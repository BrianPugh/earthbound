
INVERT_VWF_BUFFER:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	END_STACK_VARS
	LDA #.LOWORD(VWF_BUFFER)
	STA @LOCAL00
	LDY #32 * 26
	BRA @WORD_LOOP_CHECK
@INVERT_WORD:
	TAX
	LDA __BSS_START__,X
	EOR #$FFFF
	STA __BSS_START__,X
	DEY
	LDA @LOCAL00
	INC
	INC
	STA @LOCAL00
@WORD_LOOP_CHECK:
	CPY #0
	BNE @INVERT_WORD
	END_C_FUNCTION
