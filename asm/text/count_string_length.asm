
COUNT_STRING_LENGTH:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16
	STACK_RESERVE_PARAM_INT16
	STACK_RESERVE_RETURN_INT16
	END_STACK_VARS
	TXY
	TAX
	LDA #0
	STA @LOCAL00
	BRA @CHECK_CHAR
@NEXT_CHAR:
	LDA @LOCAL00
	INC
	STA @LOCAL00
	DEY
	INX
@CHECK_CHAR:
	LDA __BSS_START__,X
	AND #$00FF
	BEQ @DONE
	CPY #0
	BNE @NEXT_CHAR
@DONE:
	LDA @LOCAL00
	END_C_FUNCTION
