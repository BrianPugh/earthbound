
PRINT_MRSATURN_LETTER:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT32
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16
	END_STACK_VARS
	TAY
	STY @LOCAL03
	JSR GET_TEXT_X
	CMP #14
	BLTEQ @READY_TO_PRINT
	LDY @LOCAL03
	CPY #32
	BCC @READY_TO_PRINT
	JSR PRINT_NEWLINE
	JSL RESET_VWF_TEXT_STATE
	JSR GET_TEXT_X
	STA @VIRTUAL02
	JSR GET_TEXT_Y
	TAX
	LDA @VIRTUAL02
	INC
	JSR SET_FOCUS_TEXT_CURSOR
@READY_TO_PRINT:
	LDY @LOCAL03
	TYA
	SEC
	SBC #16
	TAX
	LDA f:MRSATURN_CHAR_TILE_MAP,X
	AND #$00FF
	TAX
	BNE @RENDER_VWF_CHAR
	LDA VWF_ACTIVE
	BEQ @PRINT_AS_TILE
	JSL RESET_VWF_TEXT_STATE
	JSR GET_TEXT_X
	STA @VIRTUAL02
	JSR GET_TEXT_Y
	TAX
	LDA @VIRTUAL02
	INC
	JSR SET_FOCUS_TEXT_CURSOR
@PRINT_AS_TILE:
	LDY @LOCAL03
	TYA
	JSR SET_WINDOW_TILE_ATTRIBUTE
	JMP @DONE
@RENDER_VWF_CHAR:
	LDA #1
	STA VWF_ACTIVE
	TXA
	DEC
	STA @LOCAL02
	TAX
	LDA f:MRSATURN_CHAR_WIDTH_TABLE,X
	AND #$00FF
	TAX
	STX @LOCAL01
	LDY VWF_TILE
	STY @LOCAL03
	LOADPTR MRSATURN_FONT_DATA, @VIRTUAL06
	LDA @LOCAL02
	AND #$0007
	ASL
	ASL
	ASL
	ASL
	ASL
	STA @VIRTUAL02
	LDA @LOCAL02
	AND #$00F8
	ASL
	ASL
	ASL
	ASL
	ASL
	ASL
	CLC
	ADC @VIRTUAL02
	CLC
	ADC @VIRTUAL06
	STA @VIRTUAL06
	CPX #8
	BLTEQ @RENDER_REMAINING
	MOVE_INT @VIRTUAL06, @LOCAL00
	LDA #8
	JSL RENDER_MRSATURN_VWF_CHARACTER
	LDX @LOCAL01
	TXA
	SEC
	SBC #8
	TAX
	LDA #16
	CLC
	ADC @VIRTUAL06
	STA @VIRTUAL06
@RENDER_REMAINING:
	MOVE_INT @VIRTUAL06, @LOCAL00
	TXA
	JSL RENDER_MRSATURN_VWF_CHARACTER
	LDX VWF_UPLOAD_START
	DEX
	STX @LOCAL01
	LDY @LOCAL03
	TYA
	JSL RENDER_VWF_TEXT_TILE
@FILL_TILE_LOOP:
	LDX @LOCAL01
	INX
	STX @LOCAL01
	CPX #48
	BCC @CHECK_WRAP
	LDX #0
	STX @LOCAL01
@CHECK_WRAP:
	TXA
	CLC
	ADC #400
	JSR SET_WINDOW_TILE_ATTRIBUTE
	LDX @LOCAL01
	CPX VWF_UPLOAD_START
	BNE @FILL_TILE_LOOP
	JSR GET_TEXT_X
	TAY
	STY @LOCAL01
	JSR GET_TEXT_Y
	TAX
	LDY @LOCAL01
	TYA
	DEC
	JSR SET_FOCUS_TEXT_CURSOR
@DONE:
	END_C_FUNCTION
