
FIX_ATTACKER_NAME:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT32
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16 ;int
	END_STACK_VARS
	TAY
	STY @LOCAL03
	SEP #PROC_FLAGS::ACCUM8
.IF .DEFINED(USA)
	STZ PRINT_ATTACKER_ARTICLE
.ENDIF
	STZ_BADOPT @LOCAL00
	LDX #.SIZEOF(enemy_data::name) + .SIZEOF(char_struct::name) - 2
	REP #PROC_FLAGS::ACCUM8
	LDA #.LOWORD(ATTACKER_NAME_ADJUST_SCRATCH)
	JSL MEMSET16
	LDX CURRENT_ATTACKER
	LDA a:battler::ally_or_enemy,X
	AND #$00FF
	CMP #1
	BEQ @LOAD_ENEMY_NAME
	LDX CURRENT_ATTACKER
	LDA a:battler::npc_id,X
	AND #$00FF
	BEQL @PARTY_MEMBER_NAME
@LOAD_ENEMY_NAME:
	LOADPTR ENEMY_CONFIGURATION_TABLE, @VIRTUAL06
	LDX CURRENT_ATTACKER
	LDA a:battler::id,X
	LDY #.SIZEOF(enemy_data)
	JSL MULT168
.IF .DEFINED(USA)
	INC
.ENDIF
	CLC
	ADC @VIRTUAL06
	STA @VIRTUAL06
	STA @LOCAL00
	LDA @VIRTUAL06+2
	STA @LOCAL00+2
	LDX #.SIZEOF(enemy_data::name)
	LDA #.LOWORD(ATTACKER_NAME_ADJUST_SCRATCH)
	JSR COPY_ENEMY_NAME
	TAX
	STX @LOCAL02
	LDX CURRENT_ATTACKER
	LDA a:battler::ally_or_enemy,X
	AND #$00FF
	CMP #1
	BNE @CHECK_PET_NAME
	LDY @LOCAL03
	BNE @CHECK_PLURAL_SUFFIX
	LDX CURRENT_ATTACKER
	LDA a:battler::the_flag,X
	AND #$00FF
	CMP #1
	BNE @APPEND_LETTER
	LDX CURRENT_ATTACKER
	LDA a:battler::enemy_type_id,X
	JSL FIND_NEXT_ENEMY_LETTER
	REP #PROC_FLAGS::ACCUM8
	AND #$00FF
	CMP #2
	BEQ @CHECK_PLURAL_SUFFIX
@APPEND_LETTER:
.IF .DEFINED(JPN)
	LDX CURRENT_ATTACKER
	SEP #PROC_FLAGS::ACCUM8
	LDA a:battler::the_flag,X
	CLC
	ADC #CHAR::A_ - 1
	LDX @LOCAL02
	STA __BSS_START__,X
@CHECK_PLURAL_SUFFIX:
	LDY @LOCAL03
	BEQ @CHECK_PET_NAME
	REP #PROC_FLAGS::ACCUM8
	LDA ENEMIES_IN_BATTLE
	CMP #1
	BLTEQ @CHECK_PET_NAME
	SEP #PROC_FLAGS::ACCUM8
	LDA #102
	LDX @LOCAL02
	STA __BSS_START__,X
	INX
	LDA #118
	STA __BSS_START__,X
.ELSE
	SEP #PROC_FLAGS::ACCUM8
	LDA #CHAR::SPACE
	LDX @LOCAL02
	STA __BSS_START__,X
	INX
	STX @LOCAL03
	LDA #1
	STA PRINT_ATTACKER_ARTICLE
	LDX CURRENT_ATTACKER
	LDA a:battler::the_flag,X
	CLC
	ADC #CHAR::A_ - 1
	LDX @LOCAL03
	STA a:battler::id,X
@CHECK_PLURAL_SUFFIX:
.ENDIF
@CHECK_PET_NAME:
	LDX CURRENT_ATTACKER
	REP #PROC_FLAGS::ACCUM8
	LDA a:battler::id,X
	CMP #ENEMY::MY_PET
	BNE @AFTER_PET_NAME
	PROMOTENEARPTR GAME_STATE + game_state::pet_name, @VIRTUAL06
	REP #PROC_FLAGS::ACCUM8
	MOVE_INT @VIRTUAL06, @LOCAL00
	LDX #6
	LDA #.LOWORD(ATTACKER_NAME_ADJUST_SCRATCH)
	JSL MEMCPY16
	SEP #PROC_FLAGS::ACCUM8
	STZ ATTACKER_NAME_ADJUST_SCRATCH+6
@AFTER_PET_NAME:
.IF .DEFINED(JPN)
	LDX #.SIZEOF(enemy_data::name) + .SIZEOF(char_struct::name) - 2
.ELSE
	LDX #.SIZEOF(enemy_data::name) + .SIZEOF(char_struct::name) - 3
.ENDIF
	REP #PROC_FLAGS::ACCUM8
	LDA #.LOWORD(ATTACKER_NAME_ADJUST_SCRATCH)
	JSL SET_BATTLE_ATTACKER_NAME_FAR
.IF .DEFINED(USA)
	LDX CURRENT_ATTACKER
	LDA a:battler::id,X
	STA ATTACKER_ENEMY_ID
.ENDIF
	BRA @RETURN
@PARTY_MEMBER_NAME:
	LDX CURRENT_ATTACKER
	LDA a:battler::id,X
	CMP #4
	BGT @RETURN
	LDX #.SIZEOF(char_struct::name)
	STX @LOCAL01
	LDX CURRENT_ATTACKER
	LDA a:battler::row,X
	AND #$00FF
	LDY #.SIZEOF(char_struct)
	JSL MULT168
	CLC
	ADC #.LOWORD(PARTY_CHARACTERS)
	LDX @LOCAL01
	JSL SET_BATTLE_ATTACKER_NAME_FAR
@RETURN:
	END_C_FUNCTION
