
CHECK_PSI_AFFLICTION_BLOCK:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_PARAM_INT16
	STACK_RESERVE_RETURN_INT16
	END_STACK_VARS
	DEC
	LDY #.SIZEOF(char_struct)
	JSL MULT168
	CLC
	ADC #.LOWORD(PARTY_CHARACTERS)+char_struct::afflictions
	TAY
	LDX #0
	BRA @LOOP_CHECK
@CHECK_GROUP:
	LDA __BSS_START__,Y
	AND #$00FF
	BEQ @NEXT_GROUP
	AND #$00FF
	DEC
	ASL
	STA @VIRTUAL02
	TXA
	STA @VIRTUAL04
	ASL
	ADC @VIRTUAL04
	ASL
	ADC @VIRTUAL04
	ASL
	CLC
	ADC @VIRTUAL02
	TAX
	LDA f:PSI_BLOCK_BY_AFFLICTION_TABLE,X
	BEQ @NOT_BLOCKED
	LDA #0
	BRA @RETURN
@NOT_BLOCKED:
	LDA #1
	BRA @RETURN
@NEXT_GROUP:
	INX
	INY
@LOOP_CHECK:
	CPX #7
	BCC @CHECK_GROUP
	LDA #1
@RETURN:
	END_C_FUNCTION
