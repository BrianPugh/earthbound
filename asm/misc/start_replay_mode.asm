
START_REPLAY_MODE:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	END_STACK_VARS
	JSL TEST_SRAM_SIZE
	CMP #0
	BEQ @INSUFFICIENT_SRAM
	LDA #1
	STA REPLAY_MODE_ACTIVE
	JSL LOAD_REPLAY_SAVE_SLOT
	LDA GAME_STATE + game_state::leader_x_coord
	STA @VIRTUAL04
	LDA GAME_STATE + game_state::leader_y_coord
	STA @VIRTUAL02
	LDX #1
	TXA
	JSL FADE_OUT
	LDX @VIRTUAL02
	LDA @VIRTUAL04
	JSL LOAD_MAP_AT_POSITION
	LDY #0
	LDX @VIRTUAL02
	LDA @VIRTUAL04
	JSL SET_LEADER_POSITION_AND_LOAD_PARTY
	JSL ENABLE_ALL_ENTITIES
	LDX #0
	LDA REPLAY_TRANSITION_STYLE
	JSL SCREEN_TRANSITION
	JSL DISABLE_ALL_ENTITIES
@INSUFFICIENT_SRAM:
	END_C_FUNCTION
