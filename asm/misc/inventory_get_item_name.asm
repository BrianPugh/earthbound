
INVENTORY_GET_ITEM_NAME:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT32
	STACK_RESERVE_INT32
	STACK_RESERVE_INT16
.IF .DEFINED(JPN)
	STACK_RESERVE_INT16
.ENDIF
	STACK_RESERVE_INT16 "LOCAL04"
	STACK_RESERVE_PARAM_INT16
	STACK_RESERVE_PARAM_INT16
	END_STACK_VARS
	TXY
	STY @LOCAL04
	TAX
	DEC
	STA @VIRTUAL04
.IF .DEFINED(JPN)
	STA @LOCAL03
.ENDIF
	TYA
	JSR CREATE_WINDOW
	LDA GAME_STATE+game_state::player_controlled_party_count
	AND #$00FF
	CMP #1
	BEQ @SINGLE_PARTY_MEMBER
	LDY @LOCAL04
	STY PAGINATION_WINDOW
@SINGLE_PARTY_MEMBER:
	LDA @VIRTUAL04
	LDY #.SIZEOF(char_struct)
	JSL MULT168
	CLC
	ADC #.LOWORD(PARTY_CHARACTERS) + char_struct::name
	PROMOTENEARPTRA @VIRTUAL06
	REP #PROC_FLAGS::ACCUM8
	MOVE_INT @VIRTUAL06, @LOCAL00
	LDX #.SIZEOF(char_struct::name)
	LDY @LOCAL04
	TYA
	JSL SET_WINDOW_TITLE
	LDA #0
	STA @VIRTUAL02
	JMP @LOOP_CHECK
@PROCESS_ITEM:
	LDA @VIRTUAL04
	LDY #.SIZEOF(char_struct)
	JSL MULT168
	CLC
	ADC #.LOWORD(PARTY_CHARACTERS)+char_struct::items
	CLC
	ADC @VIRTUAL02
	TAX
	LDA __BSS_START__,X
	AND #$00FF
	TAY
	STY @LOCAL02
.IF .DEFINED(USA)
	LDX @VIRTUAL02
	INX
	LDA @VIRTUAL04
	INC
	JSL CHECK_ITEM_EQUIPPED
	CMP #0
	BEQ @NOT_EQUIPPED
	SEP #PROC_FLAGS::ACCUM8
	LDA #CHAR::EQUIPPED
	STA TEMPORARY_TEXT_BUFFER
	REP #PROC_FLAGS::ACCUM8
	LOADPTR ITEM_CONFIGURATION_TABLE, @VIRTUAL06
	LDY @LOCAL02
.ELSE
	LOADPTR ITEM_CONFIGURATION_TABLE, @VIRTUAL06
.ENDIF
	TYA
	OPTIMIZED_MULT @VIRTUAL04, .SIZEOF(item)
	CLC
	ADC @VIRTUAL06
	STA @VIRTUAL06
	STA @LOCAL00
	LDA @VIRTUAL06+2
	STA @LOCAL00+2
	LDX #.SIZEOF(item::name)
.IF .DEFINED(JPN)
	LDA #.LOWORD(TEMPORARY_TEXT_BUFFER)
	JSL MEMCPY16
	SEP #PROC_FLAGS::ACCUM8
	STZ TEMPORARY_TEXT_BUFFER+.SIZEOF(item::name)
	LDX @VIRTUAL02
	INX
	REP #PROC_FLAGS::ACCUM8
	LDA @LOCAL03
	STA @VIRTUAL04
	INC
	JSL CHECK_ITEM_EQUIPPED
	CMP #0
	BEQ @NOT_EQUIPPED
	PROMOTENEARPTR TEMPORARY_TEXT_BUFFER, @VIRTUAL06
	REP #PROC_FLAGS::ACCUM8
	MOVE_INT @VIRTUAL06, @LOCAL00
	JSL STRLEN
	TAX
	SEP #PROC_FLAGS::ACCUM8
	LDA #CHAR::EQUIPPED
	STA TEMPORARY_TEXT_BUFFER, X
	STZ TEMPORARY_TEXT_BUFFER + 1, X
@NOT_EQUIPPED:
.ELSE
	LDA #.LOWORD(TEMPORARY_TEXT_BUFFER)+1
	JSL MEMCPY16
	BRA @NULL_TERMINATE
@NOT_EQUIPPED:
	LOADPTR ITEM_CONFIGURATION_TABLE, @VIRTUAL06
	LDY @LOCAL02
	TYA
	LDY #.SIZEOF(item)
	JSL MULT168
	CLC
	ADC @VIRTUAL06
	STA @VIRTUAL06
	STA @LOCAL00
	LDA @VIRTUAL06+2
	STA @LOCAL00+2
	LDX #.SIZEOF(item::name)
	LDA #.LOWORD(TEMPORARY_TEXT_BUFFER)
	JSL MEMCPY16
@NULL_TERMINATE:
	SEP #PROC_FLAGS::ACCUM8
	STZ TEMPORARY_TEXT_BUFFER+.SIZEOF(item::name)
.ENDIF
	LDY @LOCAL02
	BEQ @SKIP_EMPTY_SLOT
	REP #PROC_FLAGS::ACCUM8
	PROMOTENEARPTR TEMPORARY_TEXT_BUFFER, @VIRTUAL06
	REP #PROC_FLAGS::ACCUM8
	MOVE_INT @VIRTUAL06, @LOCAL00
	MOVE_INT_CONSTANT NULL, @LOCAL01
	JSR ADD_MENU_OPTION
@SKIP_EMPTY_SLOT:
	REP #PROC_FLAGS::ACCUM8
	INC @VIRTUAL02
@LOOP_CHECK:
	LDA #.SIZEOF(char_struct::items)
	CLC
	SBC @VIRTUAL02
	JUMPGTS @PROCESS_ITEM
.IF .DEFINED(USA)
	JSL WINDOW_TICK_WITHOUT_INSTANT_PRINTING
.ENDIF
	LDY #0
	TYX
	LDA #2
	JSR OPEN_WINDOW_AND_PRINT_MENU
	END_C_FUNCTION
