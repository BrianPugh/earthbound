
CALCULATE_VELOCITY_COMPONENTS:
	REP #PROC_FLAGS::ACCUM8
	PHD
	PHA
	TDC
	SEC
	SBC #$000E
	TCD
	PLA
	TXY
	PHY
	XBA
	AND #$00FC
	LSR
	PHA
	TAX
	LDA f:VELOCITY_X_TABLE,X
	CMP #$0100
	BNE @FLAG_UNSET
	TYA
	BRA @POST_FLAG
@FLAG_UNSET:
	JSR MULTIPLY_16BIT_HARDWARE
@POST_FLAG:
	PLX
	PLY
	PHA
	LDA f:VELOCITY_Y_TABLE,X
	PHX
	CMP #$0100
	BNE @FLAG2_UNSET
	TYA
	BRA @POST_FLAG2
@FLAG2_UNSET:
	JSR MULTIPLY_16BIT_HARDWARE
@POST_FLAG2:
	PLX
	CPX #$0020
	BCC @NEGATE_Y
	CPX #$0062
	BCC @Y_DONE
@NEGATE_Y:
	EOR #$FFFF
	INC
@Y_DONE:
	TAY
	PLA
	CPX #$0042
	BCC @X_DONE
	CPX #$0080
	BCS @X_DONE
	EOR #$FFFF
	INC
@X_DONE:
	TAX
	STX $16
	STY $14
	PLD
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8
	RTL
