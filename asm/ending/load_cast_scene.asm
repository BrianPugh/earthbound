
LOAD_CAST_SCENE:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT32
	STACK_RESERVE_INT32
	STACK_RESERVE_INT16
	END_STACK_VARS
	LOADPTR BUFFER, @VIRTUAL06
	STZ ITEM_TRANSFORMATIONS_LOADED
	LDY #0
	LDX #1
	TXA
	JSL FADE_OUT_WITH_MOSAIC
	JSL FORCE_BLANK_AND_WAIT_VBLANK
	JSL CLEAR_MAP_ENTITIES
	LDA #0
	STA @LOCAL02
	BRA @ENTITY_LOOP_CHECK
@PROCESS_ENTITY:
	ASL
	TAX
	LDA ENTITY_SCRIPT_TABLE,X
	CMP #.LOWORD(-1)
	BEQ @NEXT_ENTITY
	TXA
	CLC
	ADC #.LOWORD(ENTITY_SPRITEMAP_POINTER_HIGH)
	TAX
	LDA __BSS_START__,X
	ORA #$8000
	STA __BSS_START__,X
@NEXT_ENTITY:
	LDA @LOCAL02
	INC
	STA @LOCAL02
@ENTITY_LOOP_CHECK:
	CMP #MAX_ENTITIES
	BCC @PROCESS_ENTITY
	LDX #BATTLEBG_LAYER::NONE
	LDA #BATTLEBG_LAYER::UNKNOWN279
	JSL LOAD_BACKGROUND_ANIMATION
	LDY #VRAM::CAST_TILES
	LDX #VRAM::CAST_TILEMAP
	TYA ;BG_TILEMAP_SIZE::NORMAL
	JSL SET_BG3_VRAM_LOCATION
	LDA #$62
	JSL SET_OAM_SIZE
	STZ BG3_X_POS
	STZ BG3_Y_POS
	STZ BG2_Y_POS
	STZ BG2_X_POS
	STZ BG1_Y_POS
	STZ BG1_X_POS
	JSL UPDATE_SCREEN
	LDA #0
	STA [@VIRTUAL06]
	COPY_TO_VRAM1P @VIRTUAL06, VRAM::CAST_TILEMAP, 2048, 3
	SEP #PROC_FLAGS::ACCUM8
	LDA #$00FF
	STA FORCE_NORMAL_FONT_FOR_LENGTH_CALCULATIONS
	REP #PROC_FLAGS::ACCUM8
	LOADPTR BUFFER, @VIRTUAL06
	MOVE_INT @VIRTUAL06, @LOCAL00
	LDX #4096
	SEP #PROC_FLAGS::ACCUM8
	LDA #0
	JSL MEMSET24
	.A16
	LOADPTR CAST_SCENE_BASE_GFX, @LOCAL00
	LOADPTR BUFFER + $200, @LOCAL01
	JSL DECOMP
	LOADPTR CAST_NAMES_GFX, @LOCAL00
	LOADPTR BUFFER + $600, @LOCAL01
	JSL DECOMP
	JSL PREPARE_DYNAMIC_CAST_NAME_TEXT
	COPY_TO_VRAM1P @VIRTUAL06, VRAM::CAST_TILES, 32768, 0
	SEP #PROC_FLAGS::ACCUM8
	STZ FORCE_NORMAL_FONT_FOR_LENGTH_CALCULATIONS
	JSL LOAD_CHARACTER_WINDOW_PALETTE
	.A16
	LOADPTR CAST_BG_PALETTE, @LOCAL00
	LDX #BPP2PALETTE_SIZE * 4
	LDA #.LOWORD(PALETTES)
	JSL MEMCPY16
	LOADPTR SPRITE_GROUP_PALETTES, @LOCAL00
	LDX #BPP4PALETTE_SIZE * 8
	LDA #.LOWORD(PALETTES) + BPP4PALETTE_SIZE * 8
	JSL MEMCPY16
	LOADPTR CAST_NAMES_PALETTE, @LOCAL00
	LOADPTR BUFFER + $7000, @LOCAL01
	JSL DECOMP
	SEP #PROC_FLAGS::ACCUM8
	LDA #PALETTE_UPLOAD::FULL
	STA PALETTE_UPLOAD_MODE
	LDA #$14
	STA TM_MIRROR
	REP #PROC_FLAGS::ACCUM8
	STZ CAST_TEXT_ROW
	STZ CAST_TILE_OFFSET
	JSL BLANK_SCREEN_AND_WAIT_VBLANK
	END_C_FUNCTION
