
WAIT_FOR_FADE_COMPLETE:
	SEP #PROC_FLAGS::ACCUM8
	LDA FADE_PARAMETERS + fade_parameters::step
	BEQ @FADE_DONE
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8
	JSL OAM_CLEAR
	JSL UPDATE_SCREEN
	JSL WAIT_UNTIL_NEXT_FRAME
	BRA WAIT_FOR_FADE_COMPLETE
@FADE_DONE:
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8
	RTL
