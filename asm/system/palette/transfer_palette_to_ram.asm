
TRANSFER_PALETTE_TO_RAM:
	REP #PROC_FLAGS::ACCUM8
	PHD
	PHA
	TDC
	SEC
	SBC #$0004
	TCD
	PLA
	LDY #$0002
	LDA [$0E],Y
	STA $00
	INY
	INY
	LDA [$0E],Y
	STA $02
	INY
	INY
	LDA [$0E],Y
	TAX
	LDA [$0E]
	AND #$01FF
	TAY
	BRA @LOOP_CHECK
@COPY_ENTRY:
	LDA [$00],Y
	STA __BSS_START__,X
	INX
	INX
@LOOP_CHECK:
	DEY
	DEY
	BPL @COPY_ENTRY
	SEP #PROC_FLAGS::ACCUM8
	LDY #$0001
	LDA [$0E],Y
	LSR
	ORA PALETTE_UPLOAD_MODE
	STA PALETTE_UPLOAD_MODE
	REP #PROC_FLAGS::ACCUM8
	PLD
	RTL
