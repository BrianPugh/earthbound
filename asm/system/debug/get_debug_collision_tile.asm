
GET_DEBUG_COLLISION_TILE:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16
	STACK_RESERVE_PARAM_INT16
	STACK_RESERVE_PARAM_INT16
	END_STACK_VARS
	STY @VIRTUAL02
	TXY
	STA @LOCAL00
	LDX #0
	LDA VIEW_ATTRIBUTE_MODE
	AND #$00FF
	BEQ @MODE_0_COLLISION
	CMP #1
	BEQ @MODE_1_DOORS
	CMP #2
	BEQL @MODE_2_TRIGGER
	JMP @RETURN
@MODE_0_COLLISION:
	LDA @LOCAL00
	AND #$0002
	BEQ @CHECK_SOLID
	LDX #$2061
	JMP @RETURN
@CHECK_SOLID:
	LDA @LOCAL00
	AND #$0001
	BEQ @CHECK_PASSABLE_80
	LDX #$2062
	BRA @RETURN
@CHECK_PASSABLE_80:
	LDA @LOCAL00
	AND #$0080
	BEQ @CHECK_PASSABLE_40
	LDX #$2063
	BRA @RETURN
@CHECK_PASSABLE_40:
	LDA @LOCAL00
	AND #$0040
	BEQ @RETURN
	LDX #$2063
	BRA @RETURN
@MODE_1_DOORS:
	LDA @LOCAL00
	AND #$0010
	BEQ @RETURN
	LDX @VIRTUAL02
	TYA
	JSL FIND_DOOR_AT_POSITION
	REP #PROC_FLAGS::ACCUM8
	AND #$00FF
	CMP #2
	BEQ @DOOR_TYPE_2
	CMP #1
	BEQ @DOOR_TYPE_1_3_4
	CMP #3
	BEQ @DOOR_TYPE_1_3_4
	CMP #4
	BEQ @DOOR_TYPE_1_3_4
	CMP #5
	BEQ @DOOR_TYPE_0_5_6_7
	CMP #0
	BEQ @DOOR_TYPE_0_5_6_7
	CMP #6
	BEQ @DOOR_TYPE_0_5_6_7
	CMP #7
	BEQ @DOOR_TYPE_0_5_6_7
	BRA @DOOR_OTHER
@DOOR_TYPE_2:
	LDX #$2461
	BRA @RETURN
@DOOR_TYPE_1_3_4:
	LDX #$2462
	BRA @RETURN
@DOOR_TYPE_0_5_6_7:
	LDX #$2463
	BRA @RETURN
@DOOR_OTHER:
	LDX #$2058
	BRA @RETURN
@MODE_2_TRIGGER:
	LDA @LOCAL00
	AND #$0020
	BEQ @RETURN
	LDX #$2261
@RETURN:
	TXA
	END_C_FUNCTION
