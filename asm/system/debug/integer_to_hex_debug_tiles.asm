
INTEGER_TO_HEX_DEBUG_TILES:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16 ;int value
	STACK_RESERVE_RETURN_INT16 ;NEAR int*
	END_STACK_VARS
	TAY
	STY @LOCAL02
	LDA #8
	JSL SBRK
	STA @VIRTUAL02
	LDX #3
	STX @LOCAL01
	BRA @NIBBLE_LOOP_CHECK
@PROCESS_NIBBLE:
	LDY @LOCAL02
	TYA
	AND #$000F
	CMP #10
	BCC @DECIMAL_DIGIT
	CLC
	ADC #7
	STA @LOCAL00
	BRA @STORE_TILE
@DECIMAL_DIGIT:
	STA @LOCAL00
@STORE_TILE:
	TXA
	ASL
	STA @VIRTUAL04
	LDA @VIRTUAL02
	CLC
	ADC @VIRTUAL04
	TAX
	LDA @LOCAL00
	CLC
	ADC #$2030 ;0 tile, priority
	STA __BSS_START__,X
	TYA
	LSR
	LSR
	LSR
	LSR
	TAY
	STY @LOCAL02
	LDX @LOCAL01
	DEX
	STX @LOCAL01
@NIBBLE_LOOP_CHECK:
	CPX #.LOWORD(-1)
	BNE @PROCESS_NIBBLE
	LDA @VIRTUAL02
	END_C_FUNCTION
