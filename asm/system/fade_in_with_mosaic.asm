
FADE_IN_WITH_MOSAIC:
	PHP
	REP #PROC_FLAGS::ACCUM8
	PHD
	PHA
	TDC
	SEC
	SBC #$0006
	TCD
	PLA
	STZ FADE_PARAMETERS
	STA $00
	STX $02
	STY $04
	SEP #PROC_FLAGS::ACCUM8
	STZ INIDISP_MIRROR
@FADE_STEP:
	SEP #PROC_FLAGS::ACCUM8
	STZ MOSAIC_MIRROR
	LDA INIDISP_MIRROR
	CLC
	ADC $00
	CMP #$000F
	REP #PROC_FLAGS::ACCUM8
	BCS @FADE_DONE
	JSR SET_INIDISP
	LDA $04
	BEQ @SKIP_MOSAIC
	JSR UPDATE_MOSAIC_FROM_BRIGHTNESS
@SKIP_MOSAIC:
	LDA $02
	JSL WAIT_FRAMES
	BRA @FADE_STEP
@FADE_DONE:
	LDA #$000F
	JSR SET_INIDISP
	PLD
	PLP
	RTL
