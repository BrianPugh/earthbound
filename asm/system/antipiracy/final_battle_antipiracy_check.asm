
FINAL_BATTLE_ANTIPIRACY_CHECK:
	REP #PROC_FLAGS::ACCUM8
	LDX #$0033
	LDA #$0000
@CHECKSUM_LOOP:
	CLC
	ADC f:CHECK_HARDWARE,X
	DEX
	BPL @CHECKSUM_LOOP
	SEC
	SBC f:ANTIPIRACY_CHECKSUM_2
	BEQ @CHECKSUM_OK
	SEP #PROC_FLAGS::ACCUM8
	.I8
	LDX #$0000
	RTS
	LDA #$0000
@WIPE_SRAM:
	STA f:SRAM,X
	INX
	BPL @WIPE_SRAM
	LDA #$0034
	STA DMA_QUEUE_INDEX
@HANG:
	BRA @HANG
@CHECKSUM_OK:
	RTL
