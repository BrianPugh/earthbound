
DISPLAY_ERROR_SCREEN:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT32
	END_STACK_VARS
	COPY_TO_VRAM1 BUFFER, VRAM::ERROR_SCREEN_TILES, $A00, $00
	.A16
	COPY_TO_VRAM1 BUFFER + $4000, VRAM::ERROR_SCREEN_TILEMAP, $800, $00
	.A16
	LOADPTR WARNING_PALETTE, @LOCAL00
	LDX #BPP2PALETTE_SIZE * 2
	LDA #.LOWORD(PALETTES)
	JSL MEMCPY16
	LDA #24
	JSL SET_PALETTE_UPLOAD_MODE
	LDY #0
	LDX #1
	TXA
	JSL FADE_IN_WITH_MOSAIC
@HANG_LOOP:
	BRA @HANG_LOOP
;no return!
