	LDY @LOCAL00
	TYA
	OPTIMIZED_MULT @VIRTUAL04, .SIZEOF(char_struct)
	TAX
	LDA PARTY_CHARACTERS + char_struct::equipment + EQUIPMENT_SLOT::BODY,X
	AND #$00FF
	BEQ @CHECK_ARMS
	AND #$00FF
	DEC
	STA @VIRTUAL04
	TXA
	CLC
	ADC #.LOWORD(PARTY_CHARACTERS) + char_struct::items
	CLC
	ADC @VIRTUAL04
	TAX
	LDA __BSS_START__,X
	AND #$00FF
	CMP @VIRTUAL02
	BNE @CHECK_ARMS
	LDA #1
	BRA @RETURN
@CHECK_ARMS:
	LDY @LOCAL00
	TYA
	OPTIMIZED_MULT @VIRTUAL04, .SIZEOF(char_struct)
	TAX
	LDA PARTY_CHARACTERS + char_struct::equipment + EQUIPMENT_SLOT::ARMS,X
	AND #$00FF
	BEQ @CHECK_OTHER
	AND #$00FF
	DEC
	STA @VIRTUAL04
	TXA
	CLC
	ADC #.LOWORD(PARTY_CHARACTERS) + char_struct::items
	CLC
	ADC @VIRTUAL04
	TAX
	LDA __BSS_START__,X
	AND #$00FF
	CMP @VIRTUAL02
	BNE @CHECK_OTHER
	LDA #1
	BRA @RETURN
@CHECK_OTHER:
	LDY @LOCAL00
	TYA
	OPTIMIZED_MULT @VIRTUAL04, .SIZEOF(char_struct)
	TAX
	LDA PARTY_CHARACTERS + char_struct::equipment + EQUIPMENT_SLOT::OTHER,X
	AND #$00FF
	BEQ @NOT_EQUIPPED
