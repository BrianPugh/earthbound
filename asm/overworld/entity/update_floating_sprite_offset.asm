
UPDATE_FLOATING_SPRITE_OFFSET:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16
	STACK_RESERVE_PARAM_INT16
	END_STACK_VARS
	STX @LOCAL00
	CMP #1
	BEQ @OFFSET_ABOVE_LEFT
	CMP #4
	BEQ @OFFSET_LEFT
	CMP #2
	BEQ @OFFSET_ABOVE
	CMP #5
	BEQL @RETURN
	CMP #3
	BEQ @OFFSET_ABOVE_RIGHT
	CMP #6
	BEQ @OFFSET_RIGHT
	BRA @RETURN
@OFFSET_ABOVE_LEFT:
	TXA
	ASL
	TAX
	LDA f:ENTITY_COLLISION_Y_OFFSET,X
	CLC
	ADC #8
	STA @VIRTUAL02
	LDA ACTIVE_MANPU_Y
	SEC
	SBC @VIRTUAL02
	STA ACTIVE_MANPU_Y
@OFFSET_LEFT:
	LDX @LOCAL00
	TXA
	ASL
	TAX
	LDA f:ENTITY_COLLISION_X_OFFSET,X
	SEC
	SBC #8
	STA @VIRTUAL02
	LDA ACTIVE_MANPU_X
	SEC
	SBC @VIRTUAL02
	STA ACTIVE_MANPU_X
	BRA @RETURN
@OFFSET_ABOVE:
	TXA
	ASL
	TAX
	LDA f:ENTITY_COLLISION_Y_OFFSET,X
	SEC
	SBC #8
	STA @VIRTUAL02
	LDA ACTIVE_MANPU_Y
	SEC
	SBC @VIRTUAL02
	STA ACTIVE_MANPU_Y
	BRA @RETURN
@OFFSET_ABOVE_RIGHT:
	TXA
	ASL
	TAX
	LDA f:ENTITY_COLLISION_Y_OFFSET,X
	CLC
	ADC #8
	STA @VIRTUAL02
	LDA ACTIVE_MANPU_Y
	SEC
	SBC @VIRTUAL02
	STA ACTIVE_MANPU_Y
@OFFSET_RIGHT:
	LDX @LOCAL00
	TXA
	ASL
	TAX
	LDA f:ENTITY_COLLISION_X_OFFSET,X
	CLC
	ADC #8
	STA @VIRTUAL02
	LDA ACTIVE_MANPU_X
	SEC
	SBC @VIRTUAL02
	STA ACTIVE_MANPU_X
@RETURN:
	END_C_FUNCTION
