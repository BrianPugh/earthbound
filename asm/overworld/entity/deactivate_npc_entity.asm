
DEACTIVATE_NPC_ENTITY:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT32
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16
	STACK_RESERVE_PARAM_INT16
	END_STACK_VARS
	TXY
	STY @LOCAL02
	TAX
	JSL FIND_ENTITY_BY_NPC_ID
	STA @LOCAL01
	CMP #.LOWORD(-1)
	BEQ @DONE
	ASL
	TAX
	LDA ENTITY_ABS_X_TABLE,X
	STA ENTITY_PREPARED_X_COORDINATE
	LDA ENTITY_ABS_Y_TABLE,X
	STA ENTITY_PREPARED_Y_COORDINATE
	LDA ENTITY_DIRECTIONS,X
	STA ENTITY_PREPARED_DIRECTION
	LDY @LOCAL02
	CPY #6
	BEQ @USE_EVENT35
	LOADPTR BRIEF_PAUSE_END_TASK, @LOCAL00
	BRA @REASSIGN_SCRIPT
@USE_EVENT35:
	LOADPTR EVENT_35, @LOCAL00
@REASSIGN_SCRIPT:
	LDY @LOCAL00+2
	LDA @LOCAL01
	TAX
	LDA @LOCAL00
	JSL REASSIGN_ENTITY_SCRIPT_FAR
@DONE:
	END_C_FUNCTION
