
COPY_4DIR_ENTITY_SPRITE:
	REP #PROC_FLAGS::ACCUM8
	PHD
	PHA
	TDC
	SEC
	SBC #$0008
	TCD
	PLA
	PHY
	STX $04
	ASL
	TAY
	LDA #$007F
	STA $06
	LDA ENTITY_GRAPHICS_PTR_HIGH,Y
	STA $02
	LDA ENTITY_GRAPHICS_PTR_LOW,Y
	STA $00
	LDA ENTITY_DIRECTIONS,Y
	ASL
	TAX
	LDA SPRITE_DIRECTION_MAPPING_4_DIRECTION,X
	BEQ @OFFSET_DONE
	TAX
	LDA $00
	CLC
@ADD_OFFSET:
	ADC #$0004
	DEX
	BNE @ADD_OFFSET
	STA $00
@OFFSET_DONE:
	LDA [$00]
	AND #$FFF0
	STA $00
	LDA ENTITY_GRAPHICS_SPRITE_BANK,Y
	STA $02
	PLY
@COPY_LOOP:
	LDA [$00],Y
	STA [$04],Y
	DEY
	DEY
	BPL @COPY_LOOP
	PLD
	RTL
