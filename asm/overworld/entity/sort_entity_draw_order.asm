
SORT_ENTITY_DRAW_ORDER:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	END_STACK_VARS
	LDA FIRST_ENTITY
	INC
	BEQL @RETURN
	LDA #0
	STA @VIRTUAL02
	TAY
	STY @LOCAL03
	BRA @ENTITY_LOOP_CHECK
@PROCESS_ENTITY:
	TYA
	ASL
	TAX
	LDA ENTITY_SCRIPT_TABLE,X
	INC
	BEQ @NEXT_ENTITY
	LDA ENTITY_DRAW_PRIORITY,X
	DEC
	BNE @CALL_DRAW_CALLBACK
	LDA ENTITY_SCREEN_Y_TABLE,X
	CLC
	ADC #8
	AND #$FE00
	BNE @CALL_DRAW_CALLBACK
	LDA @VIRTUAL02
	ASL
	TAX
	TYA
	INC
	STA ENTITY_DRAW_SORTING,X
	INC @VIRTUAL02
	BRA @NEXT_ENTITY
@CALL_DRAW_CALLBACK:
	TYA
	JSR CALL_ENTITY_DRAW_CALLBACK
@NEXT_ENTITY:
	LDY @LOCAL03
	INY
	STY @LOCAL03
@ENTITY_LOOP_CHECK:
	CPY #MAX_ENTITIES
	BNE @PROCESS_ENTITY
	LDA @VIRTUAL02
	ASL
	TAX
	LDA #.LOWORD(-1)
	STA ENTITY_DRAW_SORTING,X
	LDA @VIRTUAL02
	STA @VIRTUAL04
	BRA @SORT_PASS_CHECK
@SORT_PASS:
	LDX #0
	STX @LOCAL03
	BRA @SEARCH_LOOP_CHECK
@SKIP_ZERO:
	LDX @LOCAL03
	INX
	STX @LOCAL03
@SEARCH_LOOP_CHECK:
	TXA
	ASL
	TAX
	LDA ENTITY_DRAW_SORTING,X
	BEQ @SKIP_ZERO
	LDX @LOCAL03
	STX @VIRTUAL02
	STX @LOCAL02
	DEC
	ASL
	TAX
	LDY ENTITY_ABS_Y_TABLE,X
	BRA @SEARCH_NEXT
@COMPARE_Y:
	LDA @LOCAL01
	BEQ @SEARCH_NEXT
	DEC
	ASL
	TAX
	LDA ENTITY_ABS_Y_TABLE,X
	STA @LOCAL00
	STA @VIRTUAL02
	TYA
	CMP @VIRTUAL02
	BCS @SEARCH_NEXT
	LDA @LOCAL00
	TAY
	LDX @LOCAL03
	STX @VIRTUAL02
	LDA @VIRTUAL02
	STA @LOCAL02
@SEARCH_NEXT:
	LDX @LOCAL03
	INX
	STX @LOCAL03
	TXA
	ASL
	TAX
	LDA ENTITY_DRAW_SORTING,X
	STA @LOCAL01
	INC
	BNE @COMPARE_Y
	LDA @LOCAL02
	STA @VIRTUAL02
	ASL
	CLC
	ADC #.LOWORD(ENTITY_DRAW_SORTING)
	TAX
	STX @LOCAL01
	LDA __BSS_START__,X
	DEC
	JSR CALL_ENTITY_DRAW_CALLBACK
	LDA #0
	LDX @LOCAL01
	STA __BSS_START__,X
@SORT_PASS_CHECK:
	LDA @VIRTUAL04
	STA @LOCAL00
	LDA @VIRTUAL04
	DEC
	STA @VIRTUAL04
	LDA @LOCAL00
	BNEL @SORT_PASS
@RETURN:
	END_C_FUNCTION
