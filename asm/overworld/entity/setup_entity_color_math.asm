
SETUP_ENTITY_COLOR_MATH:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	END_STACK_VARS
	LDA CURRENT_ENTITY_SLOT
	ASL
	TAX
	LDA ENTITY_SCRIPT_VAR0_TABLE,X
	STA @LOCAL00
	STA @VIRTUAL02
	LDA #0
	CLC
	SBC @VIRTUAL02
	BRANCHGTS @POSITIVE
	LDA @LOCAL00
	TAX
	BRA @ABS_DONE
@POSITIVE:
	LDA @LOCAL00
	EOR #$FFFF
	INC
	TAX
@ABS_DONE:
	LDA @LOCAL00
	STA @VIRTUAL02
	LDA #0
	CLC
	SBC @VIRTUAL02
	BRANCHGTS @USE_SUBTRACT
	LDA #$33
	BRA @SETUP_DONE
@USE_SUBTRACT:
	LDA #$B3
@SETUP_DONE:
	JSL SETUP_COLOR_MATH_WINDOW
	END_C_FUNCTION
