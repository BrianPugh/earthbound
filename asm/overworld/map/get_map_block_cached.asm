
GET_MAP_BLOCK_CACHED:
	REP #PROC_FLAGS::ACCUM8
	PHD
	PHA
	TDC
	SEC
	SBC #$000A
	TCD
	PLA
	STA $00
	STX $02
	ORA $02
	BPL @COORDS_VALID
	LDA #$FFFF
	PLD
	RTS
@COORDS_VALID:
	LDA $00
	CMP CACHED_MAP_BLOCK_X
	BNE @CACHE_MISS
	CPX CACHED_MAP_BLOCK_Y
	BNE @CACHE_MISS
	LDA CACHED_MAP_BLOCK_ID
	PLD
	RTS
@CACHE_MISS:
	STA CACHED_MAP_BLOCK_X
	STX CACHED_MAP_BLOCK_Y
	TXA
	LSR
	LSR
	LSR
	XBA
	ORA $00
	TAY
	LDA #.HIWORD(MAP_DATA_TILE_TABLE_CHUNK_9)
	STA $06
	LDX #.LOWORD(MAP_DATA_TILE_TABLE_CHUNK_9)
	LDA $02
	AND #$0004
	BEQ @CHUNK_SELECTED
	LDX #.LOWORD(MAP_DATA_TILE_TABLE_CHUNK_10)
@CHUNK_SELECTED:
	STX $04
	LDA $02
	AND #$0007
	ASL
	ASL
	TAX
	LDA [$04],Y
	JMP (.LOWORD(MAP_TILE_ATTRIBUTE_DISPATCH_TABLE),X)
