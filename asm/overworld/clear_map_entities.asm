
CLEAR_MAP_ENTITIES:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	END_STACK_VARS
	STZ MAGIC_BUTTERFLY_SPAWNED
	STZ ENEMY_SPAWN_TOO_MANY_ENEMIES_FAILURE_COUNT
	STZ OVERWORLD_ENEMY_COUNT
	LDX #0
	STX @LOCAL00
	BRA @LOOP_CHECK
@LOOP:
	TXA
	ASL
	TAX
	LDA ENTITY_SCRIPT_TABLE,X
	INC
	CMP #2
	BLTEQ @NEXT
	LDX @LOCAL00
	CPX #23
	BEQ @NEXT
	TXA
	JSL REMOVE_ENTITY
@NEXT:
	LDX @LOCAL00
	INX
	STX @LOCAL00
@LOOP_CHECK:
	CPX #30
	BNE @LOOP
	LDA #23
	JSL DEACTIVATE_ENTITY_FAR
	END_C_FUNCTION
