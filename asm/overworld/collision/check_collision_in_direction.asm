
CHECK_COLLISION_IN_DIRECTION:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16
	END_STACK_VARS
	STA @LOCAL06
	ASL
	TAX
	LDA f:INTERACTION_X_OFFSETS,X
	STA @LOCAL05
	LDA f:INTERACTION_Y_OFFSETS,X
	STA @LOCAL04
	LDA GAME_STATE+game_state::leader_x_coord
	CLC
	ADC @LOCAL05
	STA @LOCAL03
	LDA GAME_STATE+game_state::leader_y_coord
	CLC
	ADC @LOCAL04
	STA @VIRTUAL04
	LDA PLAYER_INTANGIBILITY_FRAMES
	STA @LOCAL02
	LDA #1
	STA PLAYER_INTANGIBILITY_FRAMES
@NPC_COLLISION_LOOP:
	LDA #.LOWORD(GAME_STATE) + game_state::current_party_members
	STA @VIRTUAL02
	LDX @VIRTUAL02
	LDA __BSS_START__,X
	TAY
	LDX @VIRTUAL04
	LDA @LOCAL03
	JSL NPC_COLLISION_CHECK
	STA @LOCAL01
	CMP #$8000
	BCS @CHECK_ENTITY_COLLISION
	ASL
	TAX
	LDA ENTITY_NPC_IDS,X
	STA INTERACTING_NPC_ID
	LDA @LOCAL01
	STA INTERACTING_NPC_ENTITY
	BRA @RESTORE_INTANGIBILITY
@CHECK_ENTITY_COLLISION:
	LDA @LOCAL06
	STA @LOCAL00
	LDX @VIRTUAL02
	LDA __BSS_START__,X
	TAY
	LDX @VIRTUAL04
	LDA @LOCAL03
	JSL CHECK_ENTITY_COLLISION
	AND #$0082
	CMP #130
	BNE @RESTORE_INTANGIBILITY
	LDA @LOCAL05
	BEQ @CHECK_Y_ADJUST
	LDA @LOCAL05
	AND #$8000
	BEQ @ADJUST_X_POSITIVE
	LDX #.LOWORD(-8)
	BRA @APPLY_X_ADJUST
@ADJUST_X_POSITIVE:
	LDX #8
@APPLY_X_ADJUST:
	TXA
	CLC
	ADC @LOCAL03
	STA @LOCAL03
@CHECK_Y_ADJUST:
	LDA @LOCAL04
	BEQ @NPC_COLLISION_LOOP
	LDA @LOCAL04
	AND #$8000
	BEQ @ADJUST_Y_POSITIVE
	LDX #.LOWORD(-8)
	BRA @APPLY_Y_ADJUST
@ADJUST_Y_POSITIVE:
	LDX #8
@APPLY_Y_ADJUST:
	STX @VIRTUAL02
	LDA @VIRTUAL04
	CLC
	ADC @VIRTUAL02
	STA @VIRTUAL04
	JMP @NPC_COLLISION_LOOP
@RESTORE_INTANGIBILITY:
	LDA @LOCAL02
	STA PLAYER_INTANGIBILITY_FRAMES
	LDA INTERACTING_NPC_ID
	BEQ @CHECK_DOOR
	LDA INTERACTING_NPC_ID
	CMP #.LOWORD(-1)
	BNE @DONE
@CHECK_DOOR:
	LDA @LOCAL06
	JSL CHECK_DOOR_IN_DIRECTION
@DONE:
	LDA INTERACTING_NPC_ID
	END_C_FUNCTION
