	MOVE_INT @LOCAL02, @VIRTUAL06
	LDY #npc_config::event_script
	LDA [@VIRTUAL06],Y
	JSL CLAMP_TO_DEBUG_MAX
	STA @LOCAL07
	LDA @LOCAL04
	STA @VIRTUAL02
	STA @LOCAL00
	LDY @LOCAL03
	STY @LOCAL01
	LDY #.LOWORD(-1)
	STY @LOCAL05
	LDA @LOCAL07
	TAX
	LDY #npc_config::sprite
	LDA [@VIRTUAL06],Y
	LDY @LOCAL05
	JSL CREATE_ENTITY
	TAX
	STX @LOCAL05
	BRA @ENTITY_CREATED
@CHECK_SHOW_NPC_NORMAL:
	LDA SHOW_NPC_FLAG
	BEQ @CREATE_ENTITY_NORMAL
	LDA [@VIRTUAL06]
	AND #$00FF
	CMP #3
	BNE @ENTITY_CREATED
@CREATE_ENTITY_NORMAL:
	LDA @LOCAL04
	STA @VIRTUAL02
	STA @LOCAL00
	LDY @LOCAL03
	STY @LOCAL01
	LDY #.LOWORD(-1)
	STY @LOCAL06
	MOVE_INT @LOCAL02, @VIRTUAL06
	LDY #npc_config::event_script
	LDA [@VIRTUAL06],Y
	TAX
	LDY #npc_config::sprite
	LDA [@VIRTUAL06],Y
	LDY @LOCAL06
	JSL CREATE_ENTITY
	TAX
	STX @LOCAL05
	BRA @ENTITY_CREATED
