	STA @LOCAL05
	TYA
	LSR
	LSR
	LSR
	STA @VIRTUAL02
	LDA @LOCAL0C
	OPTIMIZED_MULT @VIRTUAL04, 32
	CLC
	ADC @VIRTUAL02
	STA @VIRTUAL02
	LDA @LOCAL05
	LSR
	LSR
	LSR
	LSR
	LSR
	PHA
	LDA @VIRTUAL02
	LSR
	LSR
	LSR
	LSR
	OPTIMIZED_MULT @VIRTUAL04, 32
	PLY
	STY @VIRTUAL02
	CLC
	ADC @VIRTUAL02
	TAX
	LDA f:GLOBAL_MAP_TILESETPALETTE_DATA,X
	AND #$00FF
	LSR
	LSR
	LSR
	CMP LOADED_MAP_TILE_COMBO
	BNEL @SKIP_NPC
	LDA @LOCAL08
	JSL FIND_ENTITY_BY_NPC_ID_LINKED
	CMP #0
	BNEL @SKIP_NPC
	LDX @LOCAL07
	STX @VIRTUAL02
	LDA @VIRTUAL04
	XBA
	AND #$FF00
	CLC
	ADC @VIRTUAL02
	STA @VIRTUAL02
	STA @LOCAL04
	LDY @LOCAL06
	STY @VIRTUAL02
	LDA @LOCAL0C
	XBA
	AND #$FF00
	CLC
	ADC @VIRTUAL02
	TAY
	STY @LOCAL03
	LDA @LOCAL04
	STA @VIRTUAL02
	SEC
	SBC BG1_X_POS
	STA @LOCAL05
	TYA
	SEC
	SBC BG1_Y_POS
	TAX
	LDA DEBUG
	BEQ @CHECK_SPAWN_ENABLED_NORMAL
	LDA PAD_STATE
	AND #PAD::L_BUTTON | PAD::R_BUTTON
	BNE @DEBUG_FORCE_SPAWN
	LDA NPC_SPAWNS_ENABLED
	DEC
	BEQ @CHECK_OFFSCREEN_RANGE
@DEBUG_FORCE_SPAWN:
	LDA @LOCAL05
	CMP #256
	BCS @CHECK_OFFSCREEN_RANGE
	CPX #224
	BCCL @SKIP_NPC
	BRA @CHECK_OFFSCREEN_RANGE
@CHECK_SPAWN_ENABLED_NORMAL:
	LDA NPC_SPAWNS_ENABLED
	DEC
	BEQ @CHECK_OFFSCREEN_RANGE
	LDA @LOCAL05
	CMP #256
	BCS @CHECK_OFFSCREEN_RANGE
	CPX #224
	BCCL @SKIP_NPC
@CHECK_OFFSCREEN_RANGE:
	LDA @LOCAL05
	STA @VIRTUAL02
	LDA #.LOWORD(-64)
	CLC
	SBC @VIRTUAL02
	JUMPGTS @SKIP_NPC
	LDA @LOCAL05
	STA @VIRTUAL02
	LDA #320
	CLC
	SBC @VIRTUAL02
	JUMPLTEQS @SKIP_NPC
	STX @VIRTUAL02
	LDA #.LOWORD(-64)
	CLC
	SBC @VIRTUAL02
	JUMPGTS @SKIP_NPC
	STX @VIRTUAL02
	LDA #320
	CLC
	SBC @VIRTUAL02
	JUMPLTEQS @SKIP_NPC
	LOADPTR NPC_CONFIG_TABLE, @VIRTUAL06
	LDA @LOCAL08
	OPTIMIZED_MULT @VIRTUAL04, .SIZEOF(npc_config)
	CLC
