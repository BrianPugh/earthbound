	LDA DEBUG
	BEQ @CHECK_SHOW_NPC_NORMAL
	LDA SHOW_NPC_FLAG
	BEQ @CREATE_ENTITY_DEBUG
	LDA [@VIRTUAL06]
	AND #$00FF
	CMP #3
	BNEL @ENTITY_CREATED
