	BNEL @PHOTOGRAPH_MODE_SPAWN
	LDA DEBUG
	BEQ @CHECK_APPEARANCE_NORMAL
	SEP #PROC_FLAGS::ACCUM8
	LDY #npc_config::appearance_style
	LDA [@VIRTUAL06],Y
	REP #PROC_FLAGS::ACCUM8
	AND #$00FF
	STA @LOCAL06
	BEQ @APPEARANCE_OK
	JSL CHECK_DEBUG_MODE_1
	CMP #0
	BEQ @APPEARANCE_OK
	LDY #npc_config::event_flag
	LDA [@VIRTUAL06],Y
