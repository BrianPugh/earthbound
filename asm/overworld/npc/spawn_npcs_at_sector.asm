@CHECK_APPEARANCE_NORMAL:
	SEP #PROC_FLAGS::ACCUM8
	LDY #npc_config::appearance_style
	LDA [@VIRTUAL06],Y
	REP #PROC_FLAGS::ACCUM8
	AND #$00FF
	STA @LOCAL07
	BEQ @APPEARANCE_OK
	LDY #npc_config::event_flag
	LDA [@VIRTUAL06],Y
	JSL GET_EVENT_FLAG
	STA @VIRTUAL02
	LDA @LOCAL07
	DEC
	DEC
	EOR @VIRTUAL02
	AND #$0001
	BEQL @SKIP_NPC
