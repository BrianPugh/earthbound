	BEQ @SKIP_NPC
	TXA
	ASL
	TAX
	MOVE_INT @LOCAL02, @VIRTUAL06
	SEP #PROC_FLAGS::ACCUM8
	LDY #npc_config::direction
	LDA [@VIRTUAL06],Y
	REP #PROC_FLAGS::ACCUM8
	AND #$00FF
	STA ENTITY_DIRECTIONS,X
	LDA @LOCAL08
	STA ENTITY_NPC_IDS,X
@SKIP_NPC:
	INC @LOCAL09
@CHECK_NPC_INDEX:
	LDA @LOCAL09
	CMP @LOCAL0A
	BNEL @PROCESS_NPC
@RETURN:
	END_C_FUNCTION
