
REFRESH_MAP_AT_POSITION:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16 ;int x
	STACK_RESERVE_PARAM_INT16 ;int y
	END_STACK_VARS
	STX @LOCAL03
	STA @LOCAL02
	STA BG2_X_POS
	LDA @LOCAL02
	STA BG1_X_POS
	LDA @LOCAL03
	STA BG2_Y_POS
	LDA @LOCAL03
	STA BG1_Y_POS
	LDA @LOCAL02
	AND #$8000
	BEQ @X_POSITIVE
	LDA @LOCAL02
	LSR
	LSR
	LSR
	ORA #$E000
	STA @VIRTUAL04
	BRA @CHECK_Y_SIGN
@X_POSITIVE:
	LDA @LOCAL02
	LSR
	LSR
	LSR
	STA @VIRTUAL04
@CHECK_Y_SIGN:
	LDA @LOCAL03
	AND #$8000
	BEQ @Y_POSITIVE
	LDA @LOCAL03
	LSR
	LSR
	LSR
	ORA #$E000
	STA @VIRTUAL02
	JMP @COLUMN_LOOP_CHECK
@Y_POSITIVE:
	LDA @LOCAL03
	LSR
	LSR
	LSR
	STA @VIRTUAL02
	JMP @COLUMN_LOOP_CHECK
@SCROLL_RIGHT:
	AND #$8000
	BEQ @SCROLL_LEFT
	LDA SCREEN_LEFT_X
	INC
	STA @LOCAL01
	STA SCREEN_LEFT_X
	LDA @VIRTUAL02
	SEC
	SBC #16
	TAY
	STY @LOCAL00
	TYX
	LDA @LOCAL01
	CLC
	ADC #41
	JSR LOAD_MAP_COLUMN
	LDY @LOCAL00
	TYX
	LDA SCREEN_LEFT_X
	CLC
	ADC #41
	JSR LOAD_COLLISION_COLUMN
	LDX @VIRTUAL02
	LDA SCREEN_LEFT_X
	CLC
	ADC #32
	JSR LOAD_MAP_COLUMN_TO_VRAM
	LDX @VIRTUAL02
	DEX
	LDA SCREEN_LEFT_X
	CLC
	ADC #34
	JSL SPAWN_NPCS_IN_COLUMN
	LDA @VIRTUAL02
	SEC
	SBC #8
	TAX
	LDA SCREEN_LEFT_X
	CLC
	ADC #40
	JSL SPAWN_VERTICAL
	BRA @COLUMN_LOOP_CHECK
@SCROLL_LEFT:
	LDA SCREEN_LEFT_X
	DEC
	STA @LOCAL00
	STA SCREEN_LEFT_X
	LDA @VIRTUAL02
	SEC
	SBC #16
	TAY
	STY @LOCAL01
	TYX
	LDA @LOCAL00
	SEC
	SBC #16
	JSR LOAD_MAP_COLUMN
	LDY @LOCAL01
	TYX
	LDA SCREEN_LEFT_X
	SEC
	SBC #16
	JSR LOAD_COLLISION_COLUMN
	LDX @VIRTUAL02
	LDA SCREEN_LEFT_X
	DEC
	JSR LOAD_MAP_COLUMN_TO_VRAM
	LDX @VIRTUAL02
	DEX
	LDA SCREEN_LEFT_X
	DEC
	DEC
	DEC
	JSL SPAWN_NPCS_IN_COLUMN
	LDA @VIRTUAL02
	SEC
	SBC #8
	TAX
	LDA SCREEN_LEFT_X
	SEC
	SBC #8
	JSL SPAWN_VERTICAL
@COLUMN_LOOP_CHECK:
	LDA SCREEN_LEFT_X
	SEC
	SBC @VIRTUAL04
	BNEL @SCROLL_RIGHT
	JMP @ROW_LOOP_CHECK
@SCROLL_DOWN:
	AND #$8000
	BEQ @SCROLL_UP
	LDA SCREEN_TOP_Y
	INC
	STA @LOCAL01
	STA SCREEN_TOP_Y
	LDA @VIRTUAL04
	SEC
	SBC #16
	TAY
	STY @LOCAL00
	LDA @LOCAL01
	CLC
	ADC #41
	TAX
	TYA
	JSR LOAD_MAP_ROW
	LDA SCREEN_TOP_Y
	CLC
	ADC #41
	TAX
	LDY @LOCAL00
	TYA
	JSR LOAD_COLLISION_ROW
	LDA SCREEN_TOP_Y
	CLC
	ADC #28
	TAX
	LDA @VIRTUAL04
	JSR LOAD_MAP_ROW_TO_VRAM
	LDA SCREEN_TOP_Y
	CLC
	ADC #29
	TAX
	LDA @VIRTUAL04
	JSL SPAWN_NPCS_IN_ROW
	LDA SCREEN_TOP_Y
	CLC
	ADC #36
	TAX
	LDA @VIRTUAL04
	SEC
	SBC #8
	JSL SPAWN_HORIZONTAL
	BRA @ROW_LOOP_CHECK
@SCROLL_UP:
	LDA SCREEN_TOP_Y
	DEC
	STA @LOCAL01
	STA SCREEN_TOP_Y
	LDA @VIRTUAL04
	SEC
	SBC #16
	TAY
	STY @LOCAL00
	LDA @LOCAL01
	SEC
	SBC #16
	TAX
	TYA
	JSR LOAD_MAP_ROW
	LDA SCREEN_TOP_Y
	SEC
	SBC #16
	TAX
	LDY @LOCAL00
	TYA
	JSR LOAD_COLLISION_ROW
	LDX SCREEN_TOP_Y
	DEX
	LDA @VIRTUAL04
	JSR LOAD_MAP_ROW_TO_VRAM
	LDX SCREEN_TOP_Y
	DEX
	LDA @VIRTUAL04
	JSL SPAWN_NPCS_IN_ROW
	LDA SCREEN_TOP_Y
	SEC
	SBC #8
	TAX
	LDA @VIRTUAL04
	SEC
	SBC #8
	JSL SPAWN_HORIZONTAL
@ROW_LOOP_CHECK:
	LDA SCREEN_TOP_Y
	SEC
	SBC @VIRTUAL02
	BNEL @SCROLL_DOWN
	LDA @LOCAL02
	STA BG12_POSITION_X_COPY
	LDA @LOCAL03
	STA BG12_POSITION_Y_COPY
	END_C_FUNCTION
