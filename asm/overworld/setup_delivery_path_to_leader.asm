
SETUP_DELIVERY_PATH_TO_LEADER:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16
	STACK_RESERVE_RETURN_INT16
	END_STACK_VARS
	STA @LOCAL02
	LDY CURRENT_ENTITY_SLOT
	STY @LOCAL01
	LDX GAME_STATE+game_state::leader_y_coord
	LDA GAME_STATE+game_state::leader_x_coord
	JSL LOAD_SECTOR_ATTRS
	AND #$0007
	TAX
	LDA f:DELIVERY_SECTOR_PASSABLE_TABLE,X
	AND #$00FF
	BEQL @PATH_FAILED
	LDY @LOCAL01
	TYA
	ASL
	STA @VIRTUAL04
	CLC
	ADC #.LOWORD(ENTITY_PATHFINDING_STATES)
	STA @VIRTUAL02
	LDA #.LOWORD(-1)
	LDX @VIRTUAL02
	STA __BSS_START__,X
	LDY #48
	TYX
	LDA #1
	JSL FIND_PATH_TO_PARTY
	CMP #0
	BNEL @PATH_FAILED
	LDA #0
	LDX @VIRTUAL02
	STA __BSS_START__,X
	LDA @VIRTUAL04
	CLC
	ADC #.LOWORD(ENTITY_PATH_POINTS)
	TAX
	LDA __BSS_START__,X
	INC
	INC
	INC
	INC
	STA __BSS_START__,X
	LDA @VIRTUAL04
	CLC
	ADC #.LOWORD(ENTITY_PATH_POINT_COUNTS)
	TAY
	LDA __BSS_START__,Y
	DEC
	STA __BSS_START__,Y
	BNE @SETUP_PATH
	LDA #1
	BRA @RETURN
@SETUP_PATH:
	LDA __BSS_START__,X
	STA @VIRTUAL02
	LDA @LOCAL02
	STA @VIRTUAL04
	ASL
	ASL
	ADC @VIRTUAL04
	ASL
	ASL
	ASL
	ASL
	CLC
	ADC #.LOWORD(DELIVERY_PATHS)
	STA @LOCAL00
	STA __BSS_START__,X
	LDA __BSS_START__,Y
	TAY
	LDX #0
	STX @LOCAL01
	BRA @COPY_LOOP_CHECK
@COPY_POINT:
	LDA @LOCAL00
	PHA
	LDX @VIRTUAL02
	LDA __BSS_START__,X
	PLX
	STA __BSS_START__,X
	LDA @LOCAL00
	PHA
	LDX @VIRTUAL02
	LDA __BSS_START__+2,X
	PLX
	STA __BSS_START__+2,X
	INC @VIRTUAL02
	INC @VIRTUAL02
	INC @VIRTUAL02
	INC @VIRTUAL02
	LDA @LOCAL00
	INC
	INC
	INC
	INC
	STA @LOCAL00
	DEY
	LDX @LOCAL01
	INX
	STX @LOCAL01
@COPY_LOOP_CHECK:
	CPY #0
	BEQ @PATH_SUCCESS
	CPX #20
	BCC @COPY_POINT
@PATH_SUCCESS:
	LDA #0
	BRA @RETURN
@PATH_FAILED:
	LDA #1
@RETURN:
	END_C_FUNCTION
