	LDX @LOCAL00
	STA a:pathfinding::targets + 2,X
	LDA @LOCAL01
	PHA
	TAX
	LDA ENTITY_ABS_Y_TABLE,Y
	SEC
	SBC f:ENTITY_COLLISION_Y_OFFSET,X
	PLX
	CLC
	ADC f:SPRITE_HITBOX_ENABLE_TABLE,X
	LSR
	LSR
	LSR
	SEC
	SBC @VIRTUAL04
	AND #$003F
	LDX @LOCAL00
	STA a:pathfinding::targets,X
	INC @VIRTUAL02
@LOOP_CHECK:
	LDA @VIRTUAL02
	CMP @LOCAL03
	BCC @PROCESS_PARTY_MEMBER
	END_C_FUNCTION
