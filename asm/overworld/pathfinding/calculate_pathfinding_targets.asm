	ASL
	STA @LOCAL01
	TAX
	LDA ENTITY_ABS_X_TABLE,Y
	SEC
	SBC f:ENTITY_COLLISION_X_OFFSET,X
	LSR
	LSR
	LSR
	SEC
	SBC @LOCAL04
