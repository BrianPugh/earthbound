	STACK_RESERVE_PARAM_INT16
	END_STACK_VARS
	STY @LOCAL04
	STX @LOCAL03
	STA @LOCAL02
	LDX @PARAM03
	STX @VIRTUAL04
	LDA #0
	STA @VIRTUAL02
