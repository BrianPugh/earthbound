	CMP #MAX_ENTITIES
	BNE @SET_ENTITY_FAILED
	LDA #.LOWORD(-1)
	BRA @RETURN
@INIT_SUCCESS:
	LDA #0
	STA @LOCAL0B
	BRA @PATHFINDER_LOOP_CHECK
@PROCESS_PATHFINDER:
	OPTIMIZED_MULT @VIRTUAL04, 18
	STA @VIRTUAL02
	LDA @LOCAL11
	STA @VIRTUAL04
	CLC
	ADC @VIRTUAL02
	TAX
	LDA a:pathfinding::pathfinders + pathfinder::object_index,X
	STA @LOCAL08
	TXA
	CLC
	ADC #pathfinding::pathfinders + pathfinder::unknown10;
	TAY
	STY @LOCAL0F
	LDA __BSS_START__,Y
	BEQ @NO_PATH_FOUND
	LDA @LOCAL08
	ASL
	STA @LOCAL08
	LDA a:pathfinding::pathfinders + pathfinder::unknown12,X
	LDY #.LOWORD(ENTITY_PATH_POINTS)
	STA (@LOCAL08),Y
	LDY @LOCAL0F
	LDA __BSS_START__,Y
	LDY #.LOWORD(ENTITY_PATH_POINT_COUNTS)
	STA (@LOCAL08),Y
	BRA @NEXT_PATHFINDER
@NO_PATH_FOUND:
	LDA @LOCAL08
	ASL
	TAX
	LDA #1
	STA ENTITY_PATHFINDING_STATES,X
@NEXT_PATHFINDER:
	LDA @LOCAL0B
	INC
	STA @LOCAL0B
@PATHFINDER_LOOP_CHECK:
	LDX @LOCAL0A
	STX @VIRTUAL02
	CMP @VIRTUAL02
	BCC @PROCESS_PATHFINDER
