
CHECK_FOLLOWER_DIAGONAL_DISTANCE:
	LDX $88
	LDA ENTITY_ABS_X_TABLE,Y
	SEC
	SBC ENTITY_ABS_X_TABLE,X
	BPL @ABS_X_DONE
	EOR #$FFFF
	INC
@ABS_X_DONE:
	STA $00
	LDA ENTITY_SCRIPT_VAR5_TABLE,X
	TAX
	LDA $00
	CMP f:FOLLOWER_DIAGONAL_SPACING_THRESHOLDS,X
	BCC @RETURN
	LDX $88
	LDA ENTITY_ABS_Y_TABLE,Y
	SEC
	SBC ENTITY_ABS_Y_TABLE,X
	BPL @ABS_Y_DIFF_DONE
	EOR #$FFFF
	INC
@ABS_Y_DIFF_DONE:
	SEC
	SBC $00
	BEQ @RETURN
	BPL @ABS_EXCESS_DONE
	EOR #$FFFF
	INC
@ABS_EXCESS_DONE:
	DEC
@RETURN:
	RTS
