
UPDATE_PARTY_ENTITY_GRAPHICS:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT32
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16
	STACK_RESERVE_PARAM_INT16
	STACK_RESERVE_PARAM_INT16
	END_STACK_VARS
	STY @VIRTUAL04
	STX @VIRTUAL02
	STX @LOCAL03
	STA @LOCAL02
	LDA @VIRTUAL04
	STA MOVING_PARTY_MEMBER_ENTITY_ID
	LDY CURRENT_PARTY_MEMBER_TICK
	LDX @VIRTUAL02
	LDA @LOCAL02
	JSL GET_PARTY_MEMBER_SPRITE_ID
	STA @LOCAL01
	CMP #.LOWORD(-1)
	BNE @UPDATE_GRAPHICS
	LDA @VIRTUAL04
	ASL
	TAX
	LDA @LOCAL01
	STA ENTITY_ANIMATION_FRAME,X
	JMP @CHECK_CAMERA_MODE
@UPDATE_GRAPHICS:
	LOADPTR SPRITE_GROUPING_PTR_TABLE, @VIRTUAL0A
	LDA @LOCAL01
	ASL
	ASL
	CLC
	ADC @VIRTUAL0A
	STA @VIRTUAL0A
	DEREFERENCE_PTR_TO @VIRTUAL0A, @VIRTUAL06
	MOVE_INT @VIRTUAL06, @LOCAL00
	LDA @VIRTUAL04
	ASL
	TAX
	LDA @LOCAL00+2
	STA ENTITY_GRAPHICS_PTR_HIGH,X
	LDA @LOCAL00
	CLC
	ADC #9
	STA ENTITY_GRAPHICS_PTR_LOW,X
	LDY #8
	LDA [@LOCAL00],Y
	AND #$00FF
	STA ENTITY_GRAPHICS_SPRITE_BANK,X
	LDA @VIRTUAL02
	STA ENTITY_WALKING_STYLES,X
	LDA CURRENT_PARTY_MEMBER_TICK
	CLC
	ADC #char_struct::previous_walking_style
	TAY
	LDA __BSS_START__,Y
	PHA
	LDA @VIRTUAL02
	STA TEMP_REGISTER
	PLA
	STA @VIRTUAL02
	LDA TEMP_REGISTER
	CMP @VIRTUAL02
	BEQ @CHECK_LEADER_MOVED
	LDA @LOCAL03
	STA @VIRTUAL02
	STA __BSS_START__,Y
	TXA
	CLC
	ADC #.LOWORD(ENTITY_SCRIPT_VAR7_TABLE)
	TAX
	LDA __BSS_START__,X
	ORA #SPRITE_TABLE_10_FLAGS::WALKING_STYLE_CHANGED
	STA __BSS_START__,X
@CHECK_LEADER_MOVED:
	LDA GAME_STATE + game_state::leader_moved
	BEQ @STATIC_POSE
	LDA @LOCAL03
	STA @VIRTUAL02
	CMP #12
	BEQ @STATIC_POSE
	LDA @VIRTUAL04
	ASL
	CLC
	ADC #.LOWORD(ENTITY_SCRIPT_VAR7_TABLE)
	TAX
	LDA __BSS_START__,X
	AND #$FFFF ^ (SPRITE_TABLE_10_FLAGS::WALKING_STYLE_CHANGED | SPRITE_TABLE_10_FLAGS::STATIC_POSE | SPRITE_TABLE_10_FLAGS::FORCE_STATIC_ANIM)
	STA __BSS_START__,X
	BRA @CHECK_CAMERA_MODE
@STATIC_POSE:
	LDA @VIRTUAL04
	ASL
	CLC
	ADC #.LOWORD(ENTITY_SCRIPT_VAR7_TABLE)
	TAX
	LDA __BSS_START__,X
	ORA #SPRITE_TABLE_10_FLAGS::STATIC_POSE | SPRITE_TABLE_10_FLAGS::FORCE_STATIC_ANIM
	STA __BSS_START__,X
@CHECK_CAMERA_MODE:
	LDA GAME_STATE + game_state::camera_mode
	CMP #2
	BNE @RETURN
	LDA @VIRTUAL04
	ASL
	CLC
	ADC #.LOWORD(ENTITY_SCRIPT_VAR7_TABLE)
	TAX
	LDA __BSS_START__,X
	ORA #SPRITE_TABLE_10_FLAGS::SPACING_HIDE
	STA __BSS_START__,X
@RETURN:
	END_C_FUNCTION
