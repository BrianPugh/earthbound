	STA @VIRTUAL02
	ASL
	TAY
	LDA #0
	STA (@LOCAL03),Y
@ADVANCE_COLUMN:
	LDA @VIRTUAL02
	INC
	AND #$000F
	STA @VIRTUAL02
	STA @LOCAL01
	LDY @LOCAL04
	INY
	STY @LOCAL04
	INC @LOCAL00
@LOOP_CHECK:
	LDA @LOCAL00
	CMP #16
	BCC @LOOP_BODY
	BRA @DONE
@OUT_OF_BOUNDS_CLEAR:
	LDA #0
	STA @LOCAL05
	BRA @CLEAR_LOOP_CHECK
@CLEAR_LOOP_BODY:
	ASL
	TAY
	LDA #0
	STA (@LOCAL03),Y
	LDA @LOCAL05
	INC
	STA @LOCAL05
@CLEAR_LOOP_CHECK:
	CMP #16
	BCC @CLEAR_LOOP_BODY
@DONE:
	END_C_FUNCTION
