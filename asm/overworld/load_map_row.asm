	BEQ @X_POSITIVE
	LDA @LOCAL05
	LSR
	LSR
	ORA #$E000
	TAY
	STY @LOCAL04
	BRA @X_SIGN_DONE
@X_POSITIVE:
	LDA @LOCAL05
	LSR
	LSR
	TAY
	STY @LOCAL04
@X_SIGN_DONE:
	TYA
	AND #$000F
	TAX
	STX @LOCAL05
	TYA
	SEP #PROC_FLAGS::ACCUM8
	STA LOADED_ROWS_X,X
	REP #PROC_FLAGS::ACCUM8
	LDA @VIRTUAL04
	AND #$000F
	STA @LOCAL03
	TAX
	LDA @VIRTUAL04
	SEP #PROC_FLAGS::ACCUM8
	STA LOADED_ROWS_Y,X
	REP #PROC_FLAGS::ACCUM8
	TYA
	LSR
	LSR
	LSR
