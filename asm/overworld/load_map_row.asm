	LSR
	LSR
	REP #PROC_FLAGS::ACCUM8
	AND #$00FF
	STA @LOCAL02
	LDA @LOCAL03
	OPTIMIZED_MULT @VIRTUAL04, 32
	CLC
	ADC #.LOWORD(LOADED_MAP_BLOCKS)
	STA @LOCAL03
	LDA @VIRTUAL04
	CMP #$0140
	BCC @IN_BOUNDS
	JMP @OUT_OF_BOUNDS_CLEAR
@IN_BOUNDS:
	LDX @LOCAL05
	STX @VIRTUAL02
	LDA @VIRTUAL02
	STA @LOCAL01
	STZ @LOCAL00
	BRA @LOOP_CHECK
@LOOP_BODY:
	TYA
	AND #$0007
	BNE @PALETTE_UPDATED
	TYA
	LSR
	LSR
	LSR
	STA @VIRTUAL02
	LDA @VIRTUAL04
	AND #$FFFC
	OPTIMIZED_MULT @VIRTUAL04, 8
