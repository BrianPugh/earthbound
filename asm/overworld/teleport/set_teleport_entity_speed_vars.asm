
SET_TELEPORT_ENTITY_SPEED_VARS:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	END_STACK_VARS
	LDA #12
	SEC
	SBC PSI_TELEPORT_SPEED + fixed_point::integer
	TAX
	STX @LOCAL01
	BEQ @CLAMP_TO_MIN
	TXA
	AND #$8000
	BEQ @SPEED_VALID
@CLAMP_TO_MIN:
	LDX #1
	STX @LOCAL01
@SPEED_VALID:
	LDA #24
	STA @LOCAL00
	BRA @LOOP_CHECK
@STORE_SPEED:
	LDX @LOCAL01
	PHX
	ASL
	TAX
	PLA
	STA ENTITY_SCRIPT_VAR3_TABLE,X
	LDA @LOCAL00
	INC
	STA @LOCAL00
@LOOP_CHECK:
	CMP #29
	BCC @STORE_SPEED
	END_C_FUNCTION
