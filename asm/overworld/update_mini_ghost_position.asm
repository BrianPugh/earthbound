
UPDATE_MINI_GHOST_POSITION:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT32
	END_STACK_VARS
	LDA GAME_STATE+game_state::current_party_members
	ASL
	TAX
	LDA ENTITY_TICK_CALLBACK_HIGH,X
	AND #OBJECT_TICK_DISABLED | OBJECT_MOVE_DISABLED
	BEQ @VISIBLE
	LDA CURRENT_ENTITY_SLOT
	ASL
	TAX
	LDA #$FFFF
	STA ENTITY_ANIMATION_FRAME,X
	BRA @DONE
@VISIBLE:
	LDX #$3000
	LDA MINI_GHOST_ANGLE
	JSL CALCULATE_VELOCITY_COMPONENTS
	MOVE_INT @VIRTUAL06, @LOCAL00
	LDA CURRENT_ENTITY_SLOT
	ASL
	TAX
	LDA @LOCAL00+2
	AND #$FF00
	XBA
	BPL @SIGN_EXTEND
	ORA #$FF00
@SIGN_EXTEND:
	CLC
	ADC GAME_STATE+game_state::leader_x_coord
	STA ENTITY_ABS_X_TABLE,X
	LDA CURRENT_ENTITY_SLOT
	ASL
	PHA
	SEP #PROC_FLAGS::ACCUM8
	LDA #10
	SEP #PROC_FLAGS::INDEX8
	TAY
	REP #PROC_FLAGS::ACCUM8
	LDA @LOCAL00
	JSL ASR16
	STA @VIRTUAL02
	LDA GAME_STATE+game_state::leader_y_coord
	SEC
	SBC #8
	CLC
	ADC @VIRTUAL02
	REP #PROC_FLAGS::INDEX8
	PLX
	STA ENTITY_ABS_Y_TABLE,X
	LDA MINI_GHOST_ANGLE
	CLC
	ADC #$0300
	STA MINI_GHOST_ANGLE
	LDA CURRENT_ENTITY_SLOT
	ASL
	TAX
	STZ ENTITY_ANIMATION_FRAME,X
@DONE:
	END_C_FUNCTION
