
UPDATE_PARTY:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_ARRAY 6, 2 ;int[6]
	STACK_RESERVE_ARRAY 6, 2 ;int[6]
	STACK_RESERVE_ARRAY 6, 2 ;int[6]
	STACK_RESERVE_ARRAY 6, 2 ;int[6]
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
