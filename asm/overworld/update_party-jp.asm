	LDX @LOCAL08
	INX
	STX @LOCAL08
@INNER_SORT_LOOP_CHECK:
	LDA @PARTY_COUNT
	DEC
	STA @VIRTUAL02
	TXA
	CMP @VIRTUAL02
	BCC @COMPARE_ADJACENT
	INC @VIRTUAL04
@OUTER_SORT_LOOP_CHECK:
	LDA @PARTY_COUNT
	DEC
	STA @VIRTUAL02
	LDA @VIRTUAL04
	CMP @VIRTUAL02
	BCCL @SORT_INNER_INIT
	LDA #0
	STA @LOCAL06
	BRA @WRITE_SORTED_LOOP_CHECK
