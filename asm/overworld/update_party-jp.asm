	CMP @PARTY_COUNT
	BCC @SAVE_POSITION_INDEX
	LDX #0
	STX @LOCAL07
	JMP @BUILD_SORT_LOOP_CHECK
@BUILD_SORT_ENTRY:
	TXA
	CLC
	ADC #.LOWORD(GAME_STATE)
	TAX
	LDA a:game_state::party_order,X
	AND #$00FF
	STA @LOCAL06
	CMP #5
	BCC @CHECK_AFFLICTION
	CLC
	ADC #$0300
	STA @LOCAL06
	BRA @STORE_SORT_DATA
@CHECK_AFFLICTION:
	LDX @LOCAL07
	TXA
	ASL
	CLC
	ADC #.LOWORD(GAME_STATE)
	TAX
	LDA a:game_state::party_entity_slots,X
	ASL
	TAX
	LDA ENTITY_SCRIPT_VAR1_TABLE,X
	LDY #.SIZEOF(char_struct)
	JSL MULT168
	TAX
	LDA PARTY_CHARACTERS+char_struct::afflictions,X
	AND #$00FF
	TAY
	CPY #1
	BEQ @HAS_STATUS_PENALTY
	CPY #2
	BNE @STORE_SORT_DATA
@HAS_STATUS_PENALTY:
	LDA @LOCAL06
	CLC
	ADC #$0100
	STA @LOCAL06
@STORE_SORT_DATA:
	LDX @LOCAL07
	TXA
	ASL
	TAY
	LDA @LOCAL06
	TYX
	STA @LOCAL01,X
	TYA
	CLC
	ADC #.LOWORD(GAME_STATE)
	TAX
	LDA a:game_state::party_entity_slots,X
	TAX
	STX @LOCAL02,Y
	LDX @LOCAL07
	TXA
	CLC
	ADC #.LOWORD(GAME_STATE)
	TAX
	LDA a:game_state::player_controlled_party_members,X
	AND #$00FF
	TAX
	STX @LOCAL03,Y
	LDX @LOCAL07
	INX
	STX @LOCAL07
@BUILD_SORT_LOOP_CHECK:
	CPX @PARTY_COUNT
	BCCL @BUILD_SORT_ENTRY
	STZ_BADOPT @VIRTUAL04
	JMP @OUTER_SORT_LOOP_CHECK
@SORT_INNER_INIT:
	LDX #0
	STX @LOCAL08
