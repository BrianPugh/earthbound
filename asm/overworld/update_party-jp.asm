	TXA
	ASL
	TAY
	TYX
	LDA @LOCAL01,X
	STA @LOCAL05
	STY @VIRTUAL02
	INC @VIRTUAL02
	INC @VIRTUAL02
	LDX @VIRTUAL02
	LDA @LOCAL01,X
	STA @LOCAL04
	LDA @LOCAL05
	CMP @LOCAL04
	BLTEQ @NEXT_INNER
	LDX @LOCAL08
	TXA
	ASL
	TAX
	LDA @LOCAL04
	STA @LOCAL01,X
	LDX @LOCAL08
	TXA
	ASL
	TAX
	INX
	INX
	LDA @LOCAL05
	STA @LOCAL01,X
	TYX
	LDA @LOCAL02,X
	STA @LOCAL05
	LDX @LOCAL08
	TXA
	ASL
	PHA
	LDX @VIRTUAL02
	LDA @LOCAL02,X
	PLX
	STA @LOCAL02,X
	LDX @LOCAL08
	TXA
	ASL
	TAX
	INX
	INX
	LDA @LOCAL05
	STA @LOCAL02,X
	LDX @LOCAL03,Y
	TXY
	LDX @LOCAL08
	TXA
	ASL
	PHA
	LDX @VIRTUAL02
	LDA @LOCAL03,X
	PLX
	STA @LOCAL03,X
	LDX @LOCAL08
	TXA
	ASL
	TAX
	INX
	INX
	STY @LOCAL03,X
