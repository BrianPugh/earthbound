	CLC
	ADC #.LOWORD(GAME_STATE)
	TAY
	LDA @LOCAL06
	ASL
	STA @VIRTUAL02
	LDX @VIRTUAL02
	SEP #PROC_FLAGS::ACCUM8
	LDA @LOCAL01,X
	STA a:game_state::party_order,Y
	REP #PROC_FLAGS::ACCUM8
	LDA @VIRTUAL02
