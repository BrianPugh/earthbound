
IDLE_NPC_WALK_AND_SPIN:
	EVENT_SET_X $FFF8
	EVENT_SET_Y $0028
	EVENT_SET_X_VELOCITY $0100
	EVENT_SET_Y_VELOCITY $0000
@WALK_RIGHT_LOOP:
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::RIGHT, $00
	EVENT_PAUSE $08
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::RIGHT, $01
	EVENT_PAUSE $08
	EVENT_MOVEMENT_CMD_LOOP $01, .LOWORD(@WALK_RIGHT_LOOP)
	EVENT_SET_X_VELOCITY $0000
	EVENT_SET_Y_VELOCITY $0000
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN_RIGHT, $01
	EVENT_PAUSE $0A
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN, $01
	EVENT_PAUSE $0A
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN, $01
	EVENT_PAUSE $3C
@BOB_DOWN_LOOP:
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN, $00
	EVENT_PAUSE $0A
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN, $01
	EVENT_PAUSE $0A
	EVENT_MOVEMENT_CMD_LOOP $08, .LOWORD(@BOB_DOWN_LOOP)
@SPIN_LOOP:
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN_RIGHT, $01
	EVENT_PAUSE $05
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::RIGHT, $01
	EVENT_PAUSE $05
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::UP_RIGHT, $01
	EVENT_PAUSE $05
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::UP, $01
	EVENT_PAUSE $05
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::UP_LEFT, $01
	EVENT_PAUSE $05
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::LEFT, $01
	EVENT_PAUSE $05
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN_LEFT, $01
	EVENT_PAUSE $05
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN, $01
	EVENT_PAUSE $05
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN, $00
	EVENT_PAUSE $0A
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN, $01
	EVENT_PAUSE $0A
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN_LEFT, $01
	EVENT_PAUSE $05
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::LEFT, $01
	EVENT_PAUSE $05
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::UP_LEFT, $01
	EVENT_PAUSE $05
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::UP, $01
	EVENT_PAUSE $05
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::UP_RIGHT, $01
	EVENT_PAUSE $05
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::RIGHT, $01
	EVENT_PAUSE $05
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN_RIGHT, $01
	EVENT_PAUSE $05
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN, $01
	EVENT_PAUSE $05
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN, $00
	EVENT_PAUSE $0A
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN, $01
	EVENT_PAUSE $0A
@NAMING_WAIT_LOOP:
	EVENT_WRITE_WORD_WRAM .LOWORD(WAIT_FOR_NAMING_SCREEN_ACTIONSCRIPT), $0001
	EVENT_SET_ANIMATION $FF
	EVENT_PAUSE $3C
	EVENT_WRITE_WORD_WRAM .LOWORD(WAIT_FOR_NAMING_SCREEN_ACTIONSCRIPT), $0000
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN, $01
	EVENT_PAUSE $14
	EVENT_MOVEMENT_CMD_LOOP $01, .LOWORD(@NAMING_WAIT_LOOP)
@BOB_DOWN_LOOP2:
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN, $00
	EVENT_PAUSE $0A
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN, $01
	EVENT_PAUSE $0A
	EVENT_MOVEMENT_CMD_LOOP $08, .LOWORD(@BOB_DOWN_LOOP2)
	EVENT_SHORTJUMP .LOWORD(@SPIN_LOOP)
