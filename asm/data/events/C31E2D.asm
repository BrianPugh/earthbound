
MOVE_TO_TARGET_WITH_FADE_IN:
	EVENT_SET_PHYSICS_CALLBACK .LOWORD(APPLY_ENTITY_DELTA_POSITION_ENTRY2)
	EVENT_SET_ANIMATION $FF
	EVENT_SET_TICK_CALLBACK ACTIONSCRIPT_CENTRE_SCREEN_ON_ENTITY_CALLBACK
	EVENT_SET_VAR ACTIONSCRIPT_VARS::V2, $0000
	EVENT_START_TASK .LOWORD(FADE_IN_ENTITY_TASK)
	EVENT_GET_POSITION_OF_PARTY_MEMBER $FF
	EVENT_SHORTCALL .LOWORD(MOVE_TO_TARGET_LOOP)
@WAIT_FADE:
	EVENT_PAUSE 1*FRAME
	EVENT_WRITE_VAR_TO_TEMPVAR ACTIONSCRIPT_VARS::V2
	EVENT_SHORTCALL_CONDITIONAL .LOWORD(@WAIT_FADE)
	EVENT_SHORT_RETURN
FADE_IN_ENTITY_TASK:
	EVENT_SET_VAR ACTIONSCRIPT_VARS::V0, $FFF1
	EVENT_WRITE_VAR_TO_WAIT_TIMER ACTIONSCRIPT_VARS::V1
	EVENT_LOOP $0F
		EVENT_ADD ACTIONSCRIPT_VARS::V0, $0001
		EVENT_SETUP_ENTITY_COLOR_MATH
		EVENT_WRITE_VAR_TO_WAIT_TIMER ACTIONSCRIPT_VARS::V1
	EVENT_LOOP_END
	EVENT_SET_VAR ACTIONSCRIPT_VARS::V2, $0001
	EVENT_END_TASK
