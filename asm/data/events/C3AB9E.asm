
NPC_RANDOM_WANDER_LOOP:
	EVENT_GET_ENTITY_BOUNDARY_SIDE
	EVENT_SHORTCALL_CONDITIONAL .LOWORD(@CHOOSE_RANDOM_DIR)
	EVENT_ADD_TEMPVAR $FFFF
	EVENT_SHORTJUMP .LOWORD(@SET_DIR_AND_WALK)
@CHOOSE_RANDOM_DIR:
	EVENT_CHOOSE_RANDOM DIRECTION::UP, DIRECTION::RIGHT, DIRECTION::DOWN, DIRECTION::LEFT
@SET_DIR_AND_WALK:
	EVENT_SET_DIRECTION
	EVENT_SET_ENTITY_DIRECTION_VELOCITY
	EVENT_CHOOSE_RANDOM $0008, $0010
	EVENT_CALCULATE_TRAVEL_FRAMES
	EVENT_SET_VELOCITIES_ZERO
	EVENT_CHOOSE_RANDOM $001E, $003C, $005A, $0078
	EVENT_WRITE_TEMPVAR_WAITTIMER
	EVENT_SHORTJUMP .LOWORD(NPC_RANDOM_WANDER_LOOP)
