
INIT_ENEMY_PATHFINDING:
	EVENT_SET_PHYSICS_CALLBACK .LOWORD(APPLY_ENTITY_DELTA_POSITION_ENTRY4)
	EVENT_CLEAR_CURRENT_ENTITY_COLLISION
	EVENT_PAUSE 1*FRAME
	EVENT_IS_ENTITY_COLLISION_ENABLED
	EVENT_SHORTCALL_CONDITIONAL_NOT .LOWORD(ENEMY_PATHFINDING_DONE)
	EVENT_SET_TICK_CALLBACK ENTITY_PATHFINDING_STEP
	EVENT_SET_PHYSICS_CALLBACK .LOWORD(ENTITY_PHYSICS_WITH_COLLISION)
	EVENT_SET_ANIMATION $00
	EVENT_RENDER_ENTITY_SPRITE_ME2
	EVENT_SET_VAR ACTIONSCRIPT_VARS::V0, $0000
	EVENT_START_TASK .LOWORD(ENEMY_IDLE_NEAR_LEADER_CHECK)
ENEMY_PATHFINDING_DONE:
	EVENT_SHORT_RETURN
INIT_ENEMY_ENCOUNTER:
	EVENT_SHORTCALL .LOWORD(INIT_ENEMY_PATHFINDING)
	EVENT_START_TASK .LOWORD(ENEMY_COLLISION_CHECK_LOOP)
	EVENT_SHORT_RETURN
INIT_ENEMY_ENCOUNTER_NPC:
	EVENT_SHORTCALL .LOWORD(INIT_ENEMY_PATHFINDING)
	EVENT_START_TASK .LOWORD(ENEMY_NPC_COLLISION_CHECK_LOOP)
	EVENT_SHORT_RETURN
ENEMY_COLLISION_CHECK_LOOP:
	EVENT_CHECK_PROSPECTIVE_NPC_COLLISION
	EVENT_CHECK_ENEMY_MOVEMENT_OBSTACLES
	EVENT_HANDLE_ENEMY_CONTACT
	EVENT_SHORTCALL_CONDITIONAL_NOT .LOWORD(WAIT_FOR_BATTLE_AND_FLASH)
	EVENT_PAUSE 1*FRAME
	EVENT_SHORTJUMP .LOWORD(ENEMY_COLLISION_CHECK_LOOP)
ENEMY_NPC_COLLISION_CHECK_LOOP:
	EVENT_CHECK_PROSPECTIVE_NPC_COLLISION
	EVENT_CHECK_NPC_PLAYER_OBSTACLES
	EVENT_HANDLE_ENEMY_CONTACT
	EVENT_SHORTCALL_CONDITIONAL_NOT .LOWORD(WAIT_FOR_BATTLE_AND_FLASH)
	EVENT_PAUSE 1*FRAME
	EVENT_SHORTJUMP .LOWORD(ENEMY_NPC_COLLISION_CHECK_LOOP)
WAIT_FOR_BATTLE_AND_FLASH:
	EVENT_PAUSE 1*FRAME
	EVENT_IS_BATTLE_PENDING
	EVENT_SHORTCALL_CONDITIONAL_NOT .LOWORD(WAIT_FOR_BATTLE_AND_FLASH)
	EVENT_SET_ANIMATION $00
	EVENT_SET_VELOCITIES_ZERO
	EVENT_SET_PHYSICS_CALLBACK .LOWORD(APPLY_ENTITY_DELTA_POSITION_ENTRY4)
	EVENT_SET_VAR ACTIONSCRIPT_VARS::V0, $0001
	EVENT_PAUSE 1*FRAME
	EVENT_LOOP $03
		EVENT_SET_ANIMATION $FF
		EVENT_PAUSE 1*TWELFTH_OF_A_SECOND
		EVENT_SET_ANIMATION $00
		EVENT_PAUSE 1*TWELFTH_OF_A_SECOND
	EVENT_LOOP_END
DEALLOCATE_ENTITY_AND_END:
	EVENT_DEALLOCATE_ENTITY_SPRITE
	EVENT_END
