; [SINGLETON] Ringing phone — plays ring sound and rotates direction frames in a loop
EVENT_41:
	EVENT_SET_PHYSICS_CALLBACK .LOWORD(APPLY_ENTITY_DELTA_POSITION_ENTRY4)
	EVENT_SET_ANIMATION $01
	EVENT_SET_VELOCITIES_ZERO
	EVENT_UPDATE_ENTITY_SURFACE_FLAGS
@LOOP:
	EVENT_PLAY_SOUND SFX::PHONE_RING
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::UP, $01
	EVENT_PAUSE 4*MILLISECONDS
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::RIGHT, $01
	EVENT_PAUSE $06*FRAMES
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN, $01
	EVENT_PAUSE $06*FRAMES
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::UP, $01
	EVENT_PAUSE $06*FRAMES
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::LEFT, $01
	EVENT_PAUSE $06*FRAMES
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::UP, $01
	EVENT_PAUSE $06*FRAMES
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::RIGHT, $01
	EVENT_PAUSE $06*FRAMES
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::DOWN, $01
	EVENT_PAUSE $06*FRAMES
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::UP, $01
	EVENT_PAUSE $06*FRAMES
	EVENT_SET_ENTITY_DIRECTION_AND_FRAME DIRECTION::LEFT, $01
	EVENT_PAUSE $06*FRAMES
	EVENT_SHORTJUMP .LOWORD(@LOOP)
