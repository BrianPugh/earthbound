@CHASE_CHECK:
	EVENT_CHECK_ENTITY_CAN_PURSUE_SHORT
	EVENT_SHORTCALL_CONDITIONAL_NOT .LOWORD(EVENT_28_ENTRY_2)
@CHASE:
	EVENT_LOOP $04
		EVENT_STORE_LEADER_POS_TO_ENTITY_VARS
		EVENT_CALCULATE_DIRECTION_TO_LEADER
		EVENT_SET_ENTITY_VELOCITY_FROM_DIRECTION
		EVENT_QUANTIZE_ENTITY_DIRECTION
		EVENT_SET_DIRECTION
		EVENT_PAUSE 2*FIFTEENTHS_OF_A_SECOND
	EVENT_LOOP_END
	EVENT_SET_VELOCITIES_ZERO
	EVENT_PAUSE 2*FIFTEENTHS_OF_A_SECOND
	EVENT_SHORTJUMP .LOWORD(@CHASE_CHECK)
