EVENT_28_ENTRY_2:
	EVENT_SET_VAR ACTIONSCRIPT_VARS::V0, $0001
	EVENT_SET_VELOCITIES_ZERO
@WAIT_FOR_DETECTION:
	EVENT_CHECK_ENTITY_CAN_PURSUE_SHORT
	EVENT_PAUSE 2*FIFTEENTHS_OF_A_SECOND
	EVENT_SHORTCALL_CONDITIONAL_NOT .LOWORD(@WAIT_FOR_DETECTION)
	EVENT_SET_PHYSICS_CALLBACK .LOWORD(APPLY_ENTITY_DELTA_POSITION_ENTRY4)
