; [ENEMY] Oscillating NPC pursuer — moves vertically while wobbling horizontally, then chases
EVENT_23:
	EVENT_START_TASK .LOWORD(@WOBBLE_TASK)
	EVENT_SHORTCALL .LOWORD(INIT_ENEMY_ENCOUNTER_NPC)
	EVENT_IS_ENTITY_COLLISION_ENABLED
	EVENT_SHORTCALL_CONDITIONAL_NOT .LOWORD(DEALLOCATE_ENTITY_AND_END)
	EVENT_SET_ENTITY_MOVEMENT_SPEED $00, $02
@WANDER:
	EVENT_CHECK_ENTITY_CAN_PURSUE
	EVENT_SHORTCALL_CONDITIONAL .LOWORD(@SPOTTED)
	EVENT_CHOOSE_RANDOM DIRECTION::UP, DIRECTION::DOWN
	EVENT_SET_DIRECTION
	EVENT_SET_ENTITY_DIRECTION_VELOCITY
	EVENT_MOVEMENT_CMD_CALCULATE_TRAVEL_FRAMES $0020
	EVENT_SHORTJUMP .LOWORD(@WANDER)
@SPOTTED:
	EVENT_SET_PHYSICS_CALLBACK .LOWORD(APPLY_ENTITY_DELTA_POSITION_ENTRY4)
	EVENT_LOOP $0F
		EVENT_SET_Y_RELATIVE $FFFF
		EVENT_PAUSE $01
		EVENT_SET_Y_RELATIVE $0001
		EVENT_PAUSE $01
	EVENT_LOOP_END
	EVENT_SET_PHYSICS_CALLBACK .LOWORD(ENTITY_PHYSICS_WITH_COLLISION)
	EVENT_SHORTJUMP .LOWORD(@CHASE)
@CHASE_CHECK:
	EVENT_CHECK_ENTITY_CAN_PURSUE
	EVENT_SHORTCALL_CONDITIONAL_NOT .LOWORD(@WANDER)
@CHASE:
	EVENT_STORE_LEADER_POS_TO_ENTITY_VARS
	EVENT_CALCULATE_DIRECTION_TO_LEADER
	EVENT_SET_ENTITY_VELOCITY_FROM_DIRECTION
	EVENT_QUANTIZE_ENTITY_DIRECTION
	EVENT_SET_DIRECTION
	EVENT_MOVE_ENTITY_DISTANCE $0020
	EVENT_SHORTJUMP .LOWORD(@CHASE_CHECK)
@WOBBLE_TASK:
	EVENT_LOOP $10
		EVENT_SET_X_VELOCITY_RELATIVE $0080
		EVENT_PAUSE $01
	EVENT_LOOP_END
	EVENT_LOOP $10
		EVENT_SET_X_VELOCITY_RELATIVE $FF80
		EVENT_PAUSE $01
	EVENT_LOOP_END
	EVENT_SHORTJUMP .LOWORD(@WOBBLE_TASK)
