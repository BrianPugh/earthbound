
EVENT_499_500_COMMON:
	EVENT_PAUSE 1*SECOND
	EVENT_DECREMENT_DELIVERY_TIMER
	EVENT_SHORTCALL_CONDITIONAL_NOT .LOWORD(EVENT_499_500_COMMON)
	EVENT_SHORTJUMP .LOWORD(DELIVERY_CHECK_PLAYER_BUSY)
DELIVERY_CHECK_LIMIT_AND_RETRY:
	EVENT_CHECK_DELIVERY_ATTEMPT_LIMIT
	EVENT_SHORTCALL_CONDITIONAL .LOWORD(DELIVERY_FAILURE_AND_DESPAWN)
	EVENT_GET_TIMED_DELIVERY_TIME
	EVENT_LOOP_TEMPVAR
		EVENT_PAUSE 1*SECOND
	EVENT_LOOP_END
DELIVERY_CHECK_PLAYER_BUSY:
	EVENT_IS_PLAYER_BUSY
	EVENT_SHORTCALL_CONDITIONAL .LOWORD(DELIVERY_SETUP_REVERSE_PATH)
	EVENT_SHORTJUMP .LOWORD(DELIVERY_CHECK_LIMIT_AND_RETRY)
DELIVERY_SETUP_REVERSE_PATH:
	EVENT_CHECK_SECTOR_USES_MINISPRITES
	EVENT_SHORTCALL_CONDITIONAL_NOT .LOWORD(DELIVERY_FAILURE_AND_DESPAWN)
	EVENT_WRITE_VAR_TO_TEMPVAR ACTIONSCRIPT_VARS::V0
	EVENT_SETUP_DELIVERY_PATH_REVERSE
	EVENT_SHORTCALL_CONDITIONAL_NOT .LOWORD(DELIVERY_CHECK_LIMIT_AND_RETRY)
	EVENT_START_TASK .LOWORD(DELIVERY_FAILURE_TASK)
.IF .DEFINED(USA) && (!.DEFINED(PROTOTYPE19950327))
	EVENT_PAUSE 1*FRAME
.ENDIF
	EVENT_INIT_DELIVERY_SEQUENCE
	EVENT_SHORTCALL .LOWORD(DELIVERY_INIT_ANIMATE)
	EVENT_QUEUE_DELIVERY_SUCCESS_INTERACTION
	EVENT_SHORTJUMP .LOWORD(DELIVERY_SUCCESS_EXIT)
DELIVERY_FAILURE_TASK:
	EVENT_SHORTCALL .LOWORD(DELIVERY_ENTER_PATH)
DELIVERY_FAILURE_AND_DESPAWN:
	EVENT_RESTORE_OVERWORLD_STATE
	EVENT_QUEUE_DELIVERY_FAILURE_INTERACTION
	EVENT_SHORTJUMP .LOWORD(EVENT_35)
DELIVERY_INIT_ANIMATE:
	EVENT_SET_ANIMATION $00
	EVENT_START_TASK .LOWORD(ANIMATE_BLINK_FOREVER)
	EVENT_RENDER_ENTITY_SPRITE_ME2
	EVENT_SET_VAR ACTIONSCRIPT_VARS::V2, $0016
	EVENT_SET_VAR ACTIONSCRIPT_VARS::V3, $0016
@WAIT_FOR_ENTITY_NEAR:
	EVENT_PAUSE 1*FRAME
	EVENT_WRITE_VAR_TO_TEMPVAR ACTIONSCRIPT_VARS::V4
	EVENT_SHORTCALL_CONDITIONAL_NOT .LOWORD(@RETURN)
	EVENT_IS_ENTITY_IN_RANGE_OF_LEADER
	EVENT_SHORTCALL_CONDITIONAL .LOWORD(@WAIT_FOR_ENTITY_NEAR)
@RETURN:
	EVENT_SHORT_RETURN
DELIVERY_SUCCESS_EXIT:
	EVENT_SET_VELOCITIES_ZERO
	EVENT_PAUSE 1*FRAME
	EVENT_END_LAST_TASK
	EVENT_END_LAST_TASK
	EVENT_START_TASK .LOWORD(BLINK_UNTIL_NEAR_LEADER_AND_DESPAWN)
	EVENT_WRITE_VAR_TO_TEMPVAR ACTIONSCRIPT_VARS::V0
	EVENT_SETUP_DELIVERY_PATH_FROM_ENTITY
	EVENT_SHORTCALL_CONDITIONAL_NOT .LOWORD(DELIVERY_STOP_AND_RESTORE)
	EVENT_GET_TIMED_DELIVERY_EXIT_SPEED
	EVENT_SET_ENTITY_MOVEMENT_SPEED_ME2
@ADVANCE_PATH:
	EVENT_ADVANCE_ENTITY_PATH_POINT
	EVENT_SHORTCALL_CONDITIONAL .LOWORD(DELIVERY_STOP_AND_RESTORE)
	EVENT_SHORTCALL .LOWORD(MOVE_TO_TARGET_LOOP)
	EVENT_RESTORE_ENTITY_POSITION_FROM_VARS
	EVENT_SHORTJUMP .LOWORD(@ADVANCE_PATH)
DELIVERY_STOP_AND_RESTORE:
	EVENT_SET_VELOCITIES_ZERO
	EVENT_RESTORE_OVERWORLD_STATE
	EVENT_YIELD_TO_TEXT
@DESPAWN:
	EVENT_SHORTJUMP .LOWORD(EVENT_35)
DELIVERY_ENTER_PATH:
	EVENT_GET_TIMED_DELIVERY_ENTER_SPEED
	EVENT_SET_ENTITY_MOVEMENT_SPEED_ME2
	EVENT_SET_VAR ACTIONSCRIPT_VARS::V5, $0003
	EVENT_SET_VAR ACTIONSCRIPT_VARS::V4, $0000
@ENTER_ADVANCE:
	EVENT_ADVANCE_ENTITY_PATH_POINT
	EVENT_SHORTCALL_CONDITIONAL .LOWORD(DELIVERY_WAIT_AT_DESTINATION)
	EVENT_SHORTCALL .LOWORD(MOVE_TO_TARGET_LOOP)
	EVENT_RESTORE_ENTITY_POSITION_FROM_VARS
	EVENT_SHORTJUMP .LOWORD(@ENTER_ADVANCE)
DELIVERY_WAIT_AT_DESTINATION:
	EVENT_SET_VAR ACTIONSCRIPT_VARS::V4, $0001
	EVENT_PAUSE $F0*FRAMES
	EVENT_SHORTJUMP .LOWORD(DELIVERY_WAIT_AT_DESTINATION)
