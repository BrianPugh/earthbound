; [ENEMY] Wandering pursuer — moves randomly until spotting leader, then gives chase
EVENT_21:
	EVENT_SHORTCALL .LOWORD(INIT_ENEMY_ENCOUNTER)
	EVENT_IS_ENTITY_COLLISION_ENABLED
	EVENT_SHORTCALL_CONDITIONAL_NOT .LOWORD(DEALLOCATE_ENTITY_AND_END)
WANDER_AND_PURSUE_LOOP:
	EVENT_SET_ENTITY_MOVEMENT_SPEED $00, $01
	EVENT_CHECK_ENTITY_CAN_PURSUE
	EVENT_SHORTCALL_CONDITIONAL .LOWORD(@SPOTTED)
	EVENT_RAND_HIGH_BYTE
	EVENT_SET_ENTITY_VELOCITY_FROM_DIRECTION
	EVENT_QUANTIZE_ENTITY_DIRECTION
	EVENT_SET_DIRECTION
	EVENT_MOVE_ENTITY_DISTANCE $0020
	EVENT_SET_VELOCITIES_ZERO
	EVENT_CHECK_ENTITY_CAN_PURSUE
	EVENT_SHORTCALL_CONDITIONAL .LOWORD(@SPOTTED)
	EVENT_LOOP $04
		EVENT_SET_DIRECTION8 DIRECTION::LEFT
		EVENT_PAUSE $0A
		EVENT_SET_DIRECTION8 DIRECTION::RIGHT
		EVENT_PAUSE $0A
	EVENT_LOOP_END
	EVENT_SHORTJUMP .LOWORD(WANDER_AND_PURSUE_LOOP)
@SPOTTED:
	EVENT_SET_VAR ACTIONSCRIPT_VARS::V0, $0002
	EVENT_PAUSE $0F
	EVENT_SET_VAR ACTIONSCRIPT_VARS::V0, $0000
	EVENT_SHORTJUMP .LOWORD(@CHASE)
@CHASE_CHECK:
	EVENT_CHECK_ENTITY_CAN_PURSUE
	EVENT_SHORTCALL_CONDITIONAL_NOT .LOWORD(WANDER_AND_PURSUE_LOOP)
@CHASE:
	EVENT_SET_ENTITY_MOVEMENT_SPEED $80, $01
	EVENT_STORE_LEADER_POS_TO_ENTITY_VARS
	EVENT_CALCULATE_DIRECTION_TO_LEADER
	EVENT_SET_ENTITY_VELOCITY_FROM_DIRECTION
	EVENT_QUANTIZE_ENTITY_DIRECTION
	EVENT_SET_DIRECTION
	EVENT_MOVE_ENTITY_DISTANCE $0008
	EVENT_SHORTJUMP .LOWORD(@CHASE_CHECK)
