
LOAD_SPC700_DATA:
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8
	STA SPC_DATA_PTR
	STX SPC_DATA_PTR+2
	PHB
	PEA $0000
	PLB
	PLB
	PHD
	PEA $0000
	PLD
	LDY #$0000
	LDA APUIO0
	CMP #$BBAA
	BEQ @SPC_READY
	JSR WAIT_FOR_SPC700
@SPC_READY:
	SEP #PROC_FLAGS::ACCUM8
	LDA NMITIMEN_MIRROR
	AND #$007F
	STA NMITIMEN_MIRROR
	STA f:NMITIMEN
	LDA #$00CC
	BRA @READ_HEADER
@START_BLOCK:
	LDA [<SPC_DATA_PTR],Y
	INY
	XBA
	LDA #$0000
	BRA @BYTE_ACKED
@SEND_BYTE:
	XBA
	LDA [<SPC_DATA_PTR],Y
	INY
	XBA
@WAIT_ACK:
	CMP APUIO0
	BNE @WAIT_ACK
	INC
@BYTE_ACKED:
	REP #PROC_FLAGS::ACCUM8
	STA APUIO0
	SEP #PROC_FLAGS::ACCUM8
	DEX
	BNE @SEND_BYTE
@WAIT_BLOCK_END:
	CMP APUIO0
	BNE @WAIT_BLOCK_END
@GEN_COMMAND:
	ADC #$0003
	BEQ @GEN_COMMAND
@READ_HEADER:
	PHA
	REP #PROC_FLAGS::ACCUM8
	LDA [<SPC_DATA_PTR],Y
	BNE @HAS_DATA
	TAX
	LDA #$0500
	BRA @SET_DEST
@HAS_DATA:
	TAX
	INY
	INY
	LDA [<SPC_DATA_PTR],Y
	INY
	INY
@SET_DEST:
	STA APUIO2
	SEP #PROC_FLAGS::ACCUM8
	CPX #$0001
	LDA #$0000
	ROL
	STA APUIO1
	ADC #$007F
	PLA
	STA APUIO0
@WAIT_BLOCK_ACK:
	CMP APUIO0
	BNE @WAIT_BLOCK_ACK
	BVS @START_BLOCK
	REP #PROC_FLAGS::ACCUM8
@WAIT_EXECUTE:
	LDA APUIO0
	BNE @WAIT_EXECUTE
	SEP #PROC_FLAGS::ACCUM8
	LDA NMITIMEN_MIRROR
	ORA #$0080
	STA NMITIMEN_MIRROR
	STA f:NMITIMEN
	REP #PROC_FLAGS::ACCUM8
	PLD
	PLB
	RTL
