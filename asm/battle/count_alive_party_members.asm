
COUNT_ALIVE_PARTY_MEMBERS:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_RETURN_INT16
	END_STACK_VARS
.IF .DEFINED(JPN)
	LDY #0
	STY @LOCAL01
	TYA
	STA @LOCAL00
.ELSE
	LDA #0
	STA @LOCAL01
	TAY
	STY @LOCAL00
.ENDIF
	BRA @LOOP_CHECK
@CHECK_MEMBER:
.IF .DEFINED(JPN)
	CLC
	ADC #.LOWORD(GAME_STATE)
	TAX
	LDA a:game_state::party_order,X
.ELSE
	LDA GAME_STATE + game_state::party_order,Y
.ENDIF
	AND #$00FF
	DEC
	LDY #.SIZEOF(char_struct)
	JSL MULT168
	TAX
	LDA PARTY_CHARACTERS+char_struct::afflictions,X
	AND #$00FF
	TAX
	CPX #1
	BEQ @SKIP_DEAD
	CPX #2
	BEQ @SKIP_DEAD
.IF .DEFINED(JPN)
	LDY @LOCAL01
	INY
	STY @LOCAL01
.ELSE
	LDA @LOCAL01
	INC
	STA @LOCAL01
.ENDIF
@SKIP_DEAD:
.IF .DEFINED(JPN)
	LDA @LOCAL00
	INC
	STA @LOCAL00
.ELSE
	LDY @LOCAL00
	INY
	STY @LOCAL00
.ENDIF
@LOOP_CHECK:
	LDA GAME_STATE+game_state::player_controlled_party_count
	AND #$00FF
	STA @VIRTUAL02
.IF .DEFINED(JPN)
	LDA @LOCAL00
.ELSE
	TYA
.ENDIF
	CMP @VIRTUAL02
	BCC @CHECK_MEMBER
.IF .DEFINED(JPN)
	LDY @LOCAL01
	TYA
.ELSE
	LDA @LOCAL01
.ENDIF
	PLD
	RTL
