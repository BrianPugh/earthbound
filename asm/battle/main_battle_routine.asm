	MOVE_INT BATTLE_EXP_SCRATCH, @VIRTUAL06
	CLC
	ADD_INT_ASSIGN @VIRTUAL06, @VIRTUAL0A
	MOVE_INT @VIRTUAL06, BATTLE_EXP_SCRATCH
	LDA #0
	JSL COUNT_CHARS
	STORE_INT1632 @VIRTUAL0A
	JSL DIVISION32
	MOVE_INT @VIRTUAL06, BATTLE_EXP_SCRATCH
	LDA CURRENT_BATTLE_GROUP
	CMP #$01C0
	BCC @NOT_BOSS_BATTLE
	LOADPTR MSG_BTL_PLAYER_WIN_BOSS, @LOCAL00
	MOVE_INT @VIRTUAL06, @LOCAL01
	JSL DISPLAY_TEXT_WAIT
	BRA @SKIP_NOT_BOSS_BATTLE
@NOT_BOSS_BATTLE:
	LOADPTR MSG_BTL_PLAYER_WIN, @LOCAL00
	MOVE_INT @VIRTUAL06, @LOCAL01
	JSL DISPLAY_TEXT_WAIT
@SKIP_NOT_BOSS_BATTLE:
	LDA ITEM_DROPPED
	BEQ @DISTRIBUTE_EXP_SETUP
	SEP #PROC_FLAGS::ACCUM8
	LDA ITEM_DROPPED
	JSL SET_CURRENT_ITEM_FAR
	.A16
	DISPLAY_BATTLE_TEXT_PTR MSG_BTL_PRESENT
@DISTRIBUTE_EXP_SETUP:
	LDY #.LOWORD(BATTLERS_TABLE)
	STY @LOCAL10
	LDA #0
	STA @VIRTUAL02
	BRA @EXP_LOOP_CHECK
@DISTRIBUTE_EXP_LOOP:
	LDA a:battler::consciousness,Y
	AND #$00FF
	BEQ @NEXT_EXP_BATTLER
	LDA a:battler::ally_or_enemy,Y
	AND #$00FF
	BNE @NEXT_EXP_BATTLER
	LDA a:battler::npc_id,Y
	AND #$00FF
	BNE @NEXT_EXP_BATTLER
	LDA a:battler::afflictions,Y
	AND #$00FF
	TAX
	CPX #STATUS_0::UNCONSCIOUS
	BEQ @NEXT_EXP_BATTLER
	CPX #STATUS_0::DIAMONDIZED
	BEQ @NEXT_EXP_BATTLER
	MOVE_INT BATTLE_EXP_SCRATCH, @VIRTUAL06
	MOVE_INT @VIRTUAL06, @LOCAL00
	LDX #1
	LDA a:battler::id,Y
	JSL GAIN_EXP
@NEXT_EXP_BATTLER:
	LDY @LOCAL10
	TYA
	CLC
	ADC #.SIZEOF(battler)
	TAY
	STY @LOCAL10
	INC @VIRTUAL02
@EXP_LOOP_CHECK:
	LDA @VIRTUAL02
	CMP #BATTLER_COUNT
	BCC @DISTRIBUTE_EXP_LOOP
	LDA #1
.IF .DEFINED(JPN)
	STA @LOCAL06
.ELSE
	STA @LOCAL09
.ENDIF
@CHECK_TURN_CONTINUE:
.IF .DEFINED(JPN)
	LDA @LOCAL06
.ELSE
	LDA @LOCAL09
.ENDIF
	BEQL @EXECUTE_TURNS
@CLOSE_BATTLE_WINDOW:
	JSL REDIRECT_CLOSE_FOCUS_WINDOW
@CHECK_NEW_TURN:
.IF .DEFINED(JPN)
	LDA @LOCAL06
.ELSE
	LDA @LOCAL09
.ENDIF
	BEQL @START_TURN
@BATTLE_ENDING:
	JSL RESET_HPPP_ROLLING
@WAIT_HPPP_STABLE:
	JSL WINDOW_TICK
	JSL RESET_HPPP_METER_SPEED_IF_STABLE
	CMP #0
	BEQ @WAIT_HPPP_STABLE
	LDA MIRROR_ENEMY
	BEQL @BATTLE_CLEANUP
	LDA #.LOWORD(BATTLERS_TABLE)
.IF .DEFINED(JPN)
	STA @LOCAL0B
	LDX #0
	STX @LOCAL0C
.ELSE
	STA @LOCAL0F
	LDX #0
	STX @LOCAL0A
.ENDIF
	JMP @MIRROR_RESTORE_CHECK
@RESTORE_MIRROR_LOOP:
	TAX
	LDA a:battler::consciousness,X
	AND #$00FF
	BEQ @NEXT_MIRROR_BATTLER
.IF .DEFINED(JPN)
	LDA @LOCAL0B
.ELSE
	LDA @LOCAL0F
.ENDIF
	TAX
	LDA a:battler::ally_or_enemy,X
	AND #$00FF
	BNE @NEXT_MIRROR_BATTLER
.IF .DEFINED(JPN)
	LDA @LOCAL0B
.ELSE
	LDA @LOCAL0F
.ENDIF
	TAX
	LDA a:battler::id,X
	CMP #PARTY_MEMBER::POO
	BNE @NEXT_MIRROR_BATTLER
	STZ MIRROR_ENEMY
.IF .DEFINED(JPN)
	LDA @LOCAL0B
.ELSE
	LDA @LOCAL0F
.ENDIF
	CLC
	ADC #battler::afflictions
	TAX
.IF .DEFINED(JPN)
	STX @LOCAL0C
.ELSE
	STX @LOCAL0A
.ENDIF
	LDA a:STATUS_GROUP::PERSISTENT_EASYHEAL,X
	AND #$00FF
	STA @VIRTUAL04
	STA @LOCAL08
.IF .DEFINED(JPN)
	LDA @LOCAL0B
.ELSE
	LDA @LOCAL0F
.ENDIF
	PROMOTENEARPTRA @VIRTUAL06
	REP #PROC_FLAGS::ACCUM8
	MOVE_INT @VIRTUAL06, @LOCAL00
	PROMOTENEARPTR MIRROR_BATTLER_BACKUP, @VIRTUAL06
	REP #PROC_FLAGS::ACCUM8
	MOVE_INT @VIRTUAL06, @LOCAL01
	JSL COPY_MIRROR_DATA
	LDA @VIRTUAL04
	SEP #PROC_FLAGS::ACCUM8
.IF .DEFINED(JPN)
	LDX @LOCAL0C
.ELSE
	LDX @LOCAL0A
.ENDIF
	STA a:STATUS_GROUP::PERSISTENT_EASYHEAL,X
	JSL CHECK_DEAD_PLAYERS
	BRA @BATTLE_CLEANUP
@NEXT_MIRROR_BATTLER:
	.A16
.IF .DEFINED(JPN)
	LDA @LOCAL0B
.ELSE
	LDA @LOCAL0F
.ENDIF
	CLC
	ADC #.SIZEOF(battler)
.IF .DEFINED(JPN)
	STA @LOCAL0B
	LDX @LOCAL0C
	INX
	STX @LOCAL0C
.ELSE
	STA @LOCAL0F
	LDX @LOCAL0A
	INX
	STX @LOCAL0A
.ENDIF
@MIRROR_RESTORE_CHECK:
	CPX #BATTLER_COUNT
	BCCL @RESTORE_MIRROR_LOOP
@BATTLE_CLEANUP:
	JSL RESET_POST_BATTLE_STATS
	SEP #PROC_FLAGS::ACCUM8
	STZ GAME_STATE+game_state::auto_fight_enable
	REP #PROC_FLAGS::ACCUM8
	STZ BATTLE_MODE_FLAG
	LDA BATTLE_MODE
	BEQL @REINIT_BATTLE
	LDX #1
	TXA
	JSL FADE_OUT
	BRA @FADE_OUT_CHECK
@FADE_OUT_LOOP:
	JSL WAIT_UNTIL_NEXT_FRAME
	JSL UPDATE_BATTLE_SCREEN_EFFECTS
@FADE_OUT_CHECK:
	LDA FADE_PARAMETERS + fade_parameters::step
	AND #$00FF
	BNE @FADE_OUT_LOOP
	JSL CLEAR_HPPP_WINDOW_HEADER
	JSL FORCE_BLANK_AND_WAIT_VBLANK
	JSL CLOSE_ALL_WINDOWS_AND_HIDE_HPPP
	JSL CLEAR_BATTLE_VISUAL_EFFECTS
	LDA @LOCAL03
	END_C_FUNCTION
