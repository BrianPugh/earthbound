
BATTLE_ROUTINE:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT32
	STACK_RESERVE_INT32
	STACK_RESERVE_INT8
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_RETURN_INT16 ;bool
	END_STACK_VARS
	LDA BATTLE_MODE
	BNE @INIT_GIYGAS
	LDA #1
	STA @LOCAL12
	STA @LOCAL11
	SEP #PROC_FLAGS::ACCUM8
	STA GAME_STATE+game_state::player_controlled_party_count
.IF .DEFINED(JPN)
	REP #PROC_FLAGS::ACCUM8
	LDA #.LOWORD(GAME_STATE) + game_state::party_members
	STA @VIRTUAL02
	SEP #PROC_FLAGS::ACCUM8
.ELSE
	LDY #.LOWORD(GAME_STATE) + game_state::party_members
	STY @LOCAL10
.ENDIF
	STZ_BADOPT @LOCAL00
	LDX #6
	REP #PROC_FLAGS::ACCUM8
.IF .DEFINED(JPN)
	LDA @VIRTUAL02
.ELSE
	TYA
.ENDIF
	JSL MEMSET16
.IF .DEFINED(JPN)
	LDY #.LOWORD(GAME_STATE) + game_state::party_order
	STY @LOCAL10
.ELSE
	LDA #.LOWORD(GAME_STATE) + game_state::party_order
	STA @VIRTUAL02
.ENDIF
	SEP #PROC_FLAGS::ACCUM8
	STZ_BADOPT @LOCAL00
	LDX #6
	REP #PROC_FLAGS::ACCUM8
.IF .DEFINED(JPN)
	TYA
.ELSE
	LDA @VIRTUAL02
.ENDIF
	JSL MEMSET16
	SEP #PROC_FLAGS::ACCUM8
	LDA #1
.IF .DEFINED(JPN) ;reversed order?
	LDX @VIRTUAL02
	STA __BSS_START__,X
	LDY @LOCAL10
	STA __BSS_START__,Y
.ELSE
	LDY @LOCAL10
	STA __BSS_START__,Y
	LDX @VIRTUAL02
	STA __BSS_START__,X
.ENDIF
	REP #PROC_FLAGS::ACCUM8
	LDA #1
	STA ENEMIES_IN_BATTLE
	STA CURRENT_BATTLE_GROUP
	MOVE_INT f:BTL_ENTRY_PTR_TABLE+.SIZEOF(battle_entry_ptr_entry)+battle_entry_ptr_entry::pointer, @VIRTUAL06
	LDY #1
	LDA [@VIRTUAL06],Y
	STA ENEMIES_IN_BATTLE_IDS
@INIT_GIYGAS:
	STZ GIYGAS_PHASE
	LDA CURRENT_BATTLE_GROUP
	CMP #ENEMY_GROUP::UNKNOWN_475
	BNE @LOAD_BG_TABLE
	LDA #GIYGAS_PHASES::BATTLE_STARTED
	STA GIYGAS_PHASE
@LOAD_BG_TABLE:
	LOADPTR BTL_ENTRY_BG_TABLE, @VIRTUAL06
	LDA CURRENT_BATTLE_GROUP
	ASL
	ASL
	STA @LOCAL0F
	MOVE_INTX @VIRTUAL06, @VIRTUAL0A
	CLC
	ADC @VIRTUAL0A
	STA @VIRTUAL0A
	LDA [@VIRTUAL0A]
	STA @LOCAL0E
	LDA @LOCAL0F
	INC
	INC
	CLC
	ADC @VIRTUAL06
	STA @VIRTUAL06
	LDA [@VIRTUAL06]
.IF .DEFINED(JPN)
	STA @LOCAL0F
.ELSE
	STA @LOCAL0D
.ENDIF
	LDA CURRENT_BATTLE_GROUP
	ASL
	ASL
	ASL
	CLC
	ADC #battle_entry_ptr_entry::letterbox_style
	TAX
	LDA f:BTL_ENTRY_PTR_TABLE,X
	AND #$00FF
.IF .DEFINED(JPN)
	STA @LOCAL0D
.ELSE
	STA @LOCAL0C
.ENDIF
@REINIT_BATTLE:
.IF .DEFINED(USA)
	REP #PROC_FLAGS::ACCUM8
.ENDIF
	STZ MIRROR_ENEMY
.IF .DEFINED(JPN)
	STZ @LOCAL0C
	SEP #PROC_FLAGS::ACCUM8
	STZ BATTLE_ITEM_USED ;huh... usually mother 2 optimizes this worse
	REP #PROC_FLAGS::ACCUM8
	STZ @LOCAL0B
.ELSE
	STZ @LOCAL0B
	SEP #PROC_FLAGS::ACCUM8
	LDA #0
	STA BATTLE_ITEM_USED
	REP #PROC_FLAGS::ACCUM8
	STZ @LOCAL0A
.ENDIF
	STZ BATTLE_MONEY_SCRATCH
	MOVE_INT_CONSTANT 0, BATTLE_EXP_SCRATCH
	JSL FORCE_BLANK_AND_WAIT_VBLANK
	JSL CLEAR_BATTLE_VISUAL_EFFECTS
	JSL LOAD_ENEMY_BATTLE_SPRITES
	JSL LOAD_WINDOW_GFX
.IF .DEFINED(JPN)
	COPY_TO_VRAM1 BUFFER, $6000, $3800, $00
	LDY @LOCAL0D
	LDX @LOCAL0F
.ELSE
	LDA #1
	JSL UPLOAD_TEXT_TILES_TO_VRAM
	LDY @LOCAL0C
	LDX @LOCAL0D
.ENDIF
	LDA @LOCAL0E
	JSL LOAD_BATTLE_BG
	JSL SETUP_BATTLE_ENEMY_SPRITES
	LDY #0
.IF .DEFINED(JPN)
	STY @LOCAL0A
.ELSE
	STY @LOCAL10
.ENDIF
	BRA @CLEAR_BATTLER_CHECK
@CLEAR_BATTLER_LOOP:
	SEP #PROC_FLAGS::ACCUM8
	STZ_BADOPT @LOCAL00
	LDX #.SIZEOF(battler)
	REP #PROC_FLAGS::ACCUM8
	TYA
	TXY
	JSL MULT168
	CLC
	ADC #.LOWORD(BATTLERS_TABLE)
	JSL MEMSET16
.IF .DEFINED(JPN)
	LDY @LOCAL0A
	INY
	STY @LOCAL0A
.ELSE
	LDY @LOCAL10
	INY
	STY @LOCAL10
.ENDIF
@CLEAR_BATTLER_CHECK:
	CPY #BATTLER_COUNT
	BCC @CLEAR_BATTLER_LOOP
	STZ HIGHEST_ENEMY_LEVEL_IN_BATTLE
	LDY #0
	STY @LOCAL09
	STZ @LOCAL10
	JMP @INIT_PARTY_CHECK
@INIT_PARTY_MEMBER:
.IF .DEFINED(JPN)
	LDA @LOCAL10
	CLC
	ADC #.LOWORD(GAME_STATE)
	TAX
	LDA a:game_state::party_members,X
.ELSE
	LDY #.LOWORD(GAME_STATE) + game_state::party_members
	LDA (@LOCAL10),Y
.ENDIF
	AND #$00FF
	STA @VIRTUAL04
	STA @LOCAL08
	LDA @VIRTUAL04
	BEQ @INIT_NPC_PARTY_MEMBER
	LDA @VIRTUAL04
	CMP #4
	BGT @INIT_NPC_PARTY_MEMBER
	LDA @LOCAL10
	LDY #.SIZEOF(battler)
	JSL MULT168
	CLC
	ADC #.LOWORD(BATTLERS_TABLE)
	TAX
	LDA @VIRTUAL04
	JSL BATTLE_INIT_PLAYER_STATS
	BRA @NEXT_PARTY_INIT
@INIT_NPC_PARTY_MEMBER:
	LDA @VIRTUAL04
	CMP #5
	BCC @NEXT_PARTY_INIT
	LDA @LOCAL10
	LDY #.SIZEOF(battler)
	JSL MULT168
	STA @VIRTUAL02
	CLC
	ADC #.LOWORD(BATTLERS_TABLE)
	TAX
	STX @LOCAL07
	LDA @VIRTUAL04
	ASL
	TAX
	INX
	LDA f:NPC_AI_TABLE,X
	AND #$00FF
	LDX @LOCAL07
	JSL BATTLE_INIT_ENEMY_STATS
	LDX @VIRTUAL02
	SEP #PROC_FLAGS::ACCUM8
	STZ BATTLERS_TABLE+battler::ally_or_enemy,X
	REP #PROC_FLAGS::ACCUM8
	LDA @VIRTUAL04
	SEP #PROC_FLAGS::ACCUM8
	LDX @VIRTUAL02
	STA BATTLERS_TABLE+battler::npc_id,X
	LDY @LOCAL09
	REP #PROC_FLAGS::ACCUM8
	TYA
	SEP #PROC_FLAGS::ACCUM8
	LDX @VIRTUAL02
	STA BATTLERS_TABLE+16,X
	REP #PROC_FLAGS::ACCUM8
	TYA
	ASL
.IF .DEFINED(JPN)
	CLC
	ADC #.LOWORD(GAME_STATE)
	TAX
	LDA a:game_state::party_npc_1_hp,X
.ELSE
	TAX
	LDA GAME_STATE+game_state::party_npc_1_hp,X
.ENDIF
	LDX @VIRTUAL02
	STA BATTLERS_TABLE+battler::hp_target,X
	LDX @VIRTUAL02
	STA BATTLERS_TABLE+battler::hp,X
	INY
	STY @LOCAL09
	LDX @VIRTUAL02
	STZ BATTLERS_TABLE+battler::pp_target,X
	LDX @VIRTUAL02
	STZ BATTLERS_TABLE+battler::pp,X
@NEXT_PARTY_INIT:
	INC @LOCAL10
@INIT_PARTY_CHECK:
	LDA @LOCAL10
	CMP #6
	BCCL @INIT_PARTY_MEMBER
	JSL CLAMP_ENEMIES_TO_SCREEN_WIDTH
	LDY #0
	STY @LOCAL10
	BRA @INIT_ENEMY_CHECK
@INIT_ENEMY_LOOP:
	TYA
	LDY #.SIZEOF(battler)
	JSL MULT168
	CLC
	ADC #.LOWORD(BATTLERS_TABLE) + (.SIZEOF(battler) * 8)
	TAX
	STX @LOCAL07
	LDY @LOCAL10
	TYA
	ASL
	TAX
	LDA ENEMIES_IN_BATTLE_IDS,X
	LDX @LOCAL07
	JSL BATTLE_INIT_ENEMY_STATS
	LDY @LOCAL10
	INY
	STY @LOCAL10
@INIT_ENEMY_CHECK:
	CPY ENEMIES_IN_BATTLE
	BCC @INIT_ENEMY_LOOP
	JSL LAYOUT_ENEMY_BATTLE_POSITIONS
	JSL RENDER_ALL_BATTLE_SPRITES
	JSL LOAD_CHARACTER_WINDOW_PALETTE
	LDA #24
	JSL SET_PALETTE_UPLOAD_MODE
	LDA #1
	STA BATTLE_MODE_FLAG
	LDA ENEMIES_IN_BATTLE_IDS
	LDY #.SIZEOF(enemy_data)
	JSL MULT168
	CLC
	ADC #enemy_data::music
	TAX
	LDA f:ENEMY_CONFIGURATION_TABLE,X
	AND #$00FF
	JSL CHANGE_MUSIC
	JSL BLANK_SCREEN_AND_WAIT_VBLANK
	LDX #1
	TXA
	JSL FADE_IN
	LDA BATTLE_MODE
	BNEL @CHECK_BUZZ_BUZZ
	LDA @LOCAL12
	JSL INITIALIZE_BATTLE_PARTY
	LDA #0
	STA @VIRTUAL02
	TAY
	STY @LOCAL10
	JMP @REINIT_PARTY_CHECK
@REINIT_PARTY_MEMBER:
	REP #PROC_FLAGS::ACCUM8
.IF .DEFINED(JPN)
	TYA
	CLC
	ADC #.LOWORD(GAME_STATE)
	TAX
	LDA a:game_state::party_members,X
.ELSE
	LDA GAME_STATE + game_state::party_members,Y
.ENDIF
	AND #$00FF
	STA @VIRTUAL04
	STA @LOCAL08
	LDA @VIRTUAL04
	BEQ @REINIT_NPC_MEMBER
	LDA @VIRTUAL04
	CMP #4
	BGT @REINIT_NPC_MEMBER
	TYA
	LDY #.SIZEOF(battler)
	JSL MULT168
	CLC
	ADC #.LOWORD(BATTLERS_TABLE)
	TAX
	LDA @VIRTUAL04
	JSL BATTLE_INIT_PLAYER_STATS
	JMP @REINIT_NEXT_MEMBER
@REINIT_NPC_MEMBER:
	LDA @VIRTUAL04
	CMP #5
	BCCL @REINIT_NEXT_MEMBER
	LOADPTR NPC_AI_TABLE, @VIRTUAL06
	LDA @VIRTUAL04
	ASL
.IF .DEFINED(JPN)
	STA @LOCAL09
.ELSE
	STA @LOCAL06
.ENDIF
	MOVE_INTX @VIRTUAL06, @VIRTUAL0A
	CLC
	ADC @VIRTUAL0A
	STA @VIRTUAL0A
	LDA [@VIRTUAL0A]
	AND #$00FF
	AND #$0001
	BEQ @REINIT_NEXT_MEMBER
	TYA
	LDY #.SIZEOF(battler)
	JSL MULT168
.IF .DEFINED(JPN)
	STA @LOCAL06
.ELSE
	STA @LOCAL09
.ENDIF
	CLC
	ADC #.LOWORD(BATTLERS_TABLE)
	TAX
.IF .DEFINED(JPN)
	LDA @LOCAL09
.ELSE
	LDA @LOCAL06
.ENDIF
	INC
	CLC
	ADC @VIRTUAL06
	STA @VIRTUAL06
	LDA [@VIRTUAL06]
	AND #$00FF
	JSL BATTLE_INIT_ENEMY_STATS
	LDA @VIRTUAL02
	SEP #PROC_FLAGS::ACCUM8
	LDY #.LOWORD(BATTLERS_TABLE) + battler::row
.IF .DEFINED(JPN)
	STA (@LOCAL06),Y
.ELSE
	STA (@LOCAL09),Y
.ENDIF
	REP #PROC_FLAGS::ACCUM8
	LDA @VIRTUAL02
	ASL
.IF .DEFINED(JPN)
	CLC
	ADC #.LOWORD(GAME_STATE)
	TAX
	LDA a:game_state::party_npc_1_hp,X
	LDY #.LOWORD(BATTLERS_TABLE) + battler::hp_target
	STA (@LOCAL06),Y
	LDY #.LOWORD(BATTLERS_TABLE) + battler::hp
	STA (@LOCAL06),Y
	INC @VIRTUAL02
	LDX @LOCAL06
	STZ BATTLERS_TABLE+battler::pp_target,X
	LDX @LOCAL06
	STZ BATTLERS_TABLE+battler::pp,X
	LDX @LOCAL06
.ELSE
	TAX
	LDA GAME_STATE+game_state::party_npc_1_hp,X
	LDY #.LOWORD(BATTLERS_TABLE) + battler::hp_target
	STA (@LOCAL09),Y
	LDY #.LOWORD(BATTLERS_TABLE) + battler::hp
	STA (@LOCAL09),Y
	INC @VIRTUAL02
	LDX @LOCAL09
	STZ BATTLERS_TABLE+battler::pp_target,X
	LDX @LOCAL09
	STZ BATTLERS_TABLE+battler::pp,X
	LDX @LOCAL09
.ENDIF
	SEP #PROC_FLAGS::ACCUM8
	STZ BATTLERS_TABLE+battler::ally_or_enemy,X
	REP #PROC_FLAGS::ACCUM8
	LDA @VIRTUAL04
	SEP #PROC_FLAGS::ACCUM8
	LDY #.LOWORD(BATTLERS_TABLE) + battler::npc_id
.IF .DEFINED(JPN)
	STA (@LOCAL06),Y
.ELSE
	STA (@LOCAL09),Y
.ENDIF
@REINIT_NEXT_MEMBER:
	LDY @LOCAL10
	INY
	STY @LOCAL10
@REINIT_PARTY_CHECK:
	CPY #6
	BCCL @REINIT_PARTY_MEMBER
	JSL REDIRECT_SHOW_HPPP_WINDOWS
	JSL WINDOW_TICK
@DEBUG_BATTLE_LOOP:
	.A16
	JSL WAIT_UNTIL_NEXT_FRAME
	JSL UPDATE_BATTLE_SCREEN_EFFECTS
	LDA PAD_PRESS
	AND #PAD::START_BUTTON
	BNEL @START_BATTLE_MUSIC
	LDA PAD_PRESS
	AND #PAD::SELECT_BUTTON
	BEQ @DEBUG_CHECK_RIGHT
	LDA CURRENT_BATTLE_GROUP
	JSL ENEMY_SELECT_MODE
	STA @LOCAL10
	STA CURRENT_BATTLE_GROUP
	LOADPTR BTL_ENTRY_BG_TABLE, @VIRTUAL06
	LDA @LOCAL10
	ASL
	ASL
	TAX
	MOVE_INTY @VIRTUAL06, @VIRTUAL0A
	CLC
	ADC @VIRTUAL0A
	STA @VIRTUAL0A
	LDA [@VIRTUAL0A]
	STA @LOCAL0E
	TXA
	INC
	INC
	CLC
	ADC @VIRTUAL06
	STA @VIRTUAL06
	LDA [@VIRTUAL06]
.IF .DEFINED(JPN)
	STA @LOCAL0F
.ELSE
	STA @LOCAL0D
.ENDIF
	LDA @LOCAL10
	ASL
	ASL
	ASL
	CLC
	ADC #battle_entry_ptr_entry::letterbox_style
	TAX
	LDA f:BTL_ENTRY_PTR_TABLE,X
	AND #$00FF
.IF .DEFINED(JPN)
	STA @LOCAL0D
.ELSE
	STA @LOCAL0C
.ENDIF
	JMP @APPLY_DEBUG_PARTY
@DEBUG_CHECK_RIGHT:
	LDA PAD_HELD
	AND #PAD::RIGHT
	BEQ @DEBUG_CHECK_LEFT
	LDA @LOCAL11
	CMP #15
	BCS @DEBUG_CHECK_DOWN
	INC @LOCAL11
	JMP @APPLY_DEBUG_PARTY
@DEBUG_CHECK_LEFT:
	LDA PAD_HELD
	AND #PAD::LEFT
	BEQ @DEBUG_CHECK_DOWN
	LDA @LOCAL11
	CMP #1
	BLTEQ @DEBUG_CHECK_DOWN
	DEC @LOCAL11
	JMP @APPLY_DEBUG_PARTY
@DEBUG_CHECK_DOWN:
	LDA PAD_HELD
	AND #PAD::DOWN
	BEQ @DEBUG_CHECK_UP
	LDA @LOCAL12
	CMP #1
	BLTEQ @DEBUG_CHECK_X
	DEC @LOCAL12
	BRA @APPLY_DEBUG_PARTY
@DEBUG_CHECK_UP:
	LDA PAD_HELD
	AND #PAD::UP
	BEQ @DEBUG_CHECK_X
	LDA @LOCAL12
	CMP #MAX_LEVEL
	BCS @DEBUG_CHECK_X
	INC @LOCAL12
	BRA @APPLY_DEBUG_PARTY
@DEBUG_CHECK_X:
	LDA PAD_PRESS
	AND #PAD::X_BUTTON
	BEQ @DEBUG_CHECK_A
	LDA HIGHEST_ENEMY_LEVEL_IN_BATTLE
	STA @LOCAL12
	BRA @APPLY_DEBUG_PARTY
@DEBUG_CHECK_A:
	LDA PAD_PRESS
	AND #PAD::A_BUTTON
	BEQ @DEBUG_CHECK_B
	LDA DEBUGGING_CURRENT_PSI_ANIMATION
	JSL SHOW_PSI_ANIMATION
	LDX DEBUGGING_CURRENT_PSI_ANIMATION
	INX
	STX DEBUGGING_CURRENT_PSI_ANIMATION
	CPX #34
	BNE @DEBUG_CHECK_B
	STZ DEBUGGING_CURRENT_PSI_ANIMATION
@DEBUG_CHECK_B:
	LDA PAD_PRESS
	AND #PAD::B_BUTTON
	BEQL @DEBUG_BATTLE_LOOP
	LDX DEBUGGING_CURRENT_SWIRL_FLAGS
	LDA DEBUGGING_CURRENT_SWIRL
	JSL INIT_SWIRL_EFFECT
	LDX DEBUGGING_CURRENT_SWIRL
	INX
	STX DEBUGGING_CURRENT_SWIRL
	CPX #8
	BNEL @DEBUG_BATTLE_LOOP
	STZ DEBUGGING_CURRENT_SWIRL
	LDA DEBUGGING_CURRENT_SWIRL_FLAGS
	INC
	AND #$0003
	STA DEBUGGING_CURRENT_SWIRL_FLAGS
	JMP @DEBUG_BATTLE_LOOP
@APPLY_DEBUG_PARTY:
.IF .DEFINED(JPN)
	LDA #0
	STA @LOCAL0B
.ELSE
	LDX #0
.ENDIF
	LDA @LOCAL11
	AND #$0001
	BEQ @ADD_NESS
	SEP #PROC_FLAGS::ACCUM8
	LDA #1
	STA GAME_STATE + game_state::party_members
.IF .DEFINED(JPN)
	REP #PROC_FLAGS::ACCUM8
	LDA #1
	STA @LOCAL0B
.ELSE
	LDX #1
.ENDIF
@ADD_NESS:
.IF .DEFINED(JPN)
	LDA @LOCAL11
	AND #$0002
	BEQ @ADD_PAULA
	LDA @LOCAL0B
	CLC
	ADC #.LOWORD(GAME_STATE)
	TAX
	SEP #PROC_FLAGS::ACCUM8
	LDA #2
	STA a:game_state::party_members,X
	REP #PROC_FLAGS::ACCUM8
	LDA @LOCAL0B
	INC
	STA @LOCAL0B
.ELSE
	REP #PROC_FLAGS::ACCUM8
	LDA @LOCAL11
	AND #$0002
	BEQ @ADD_PAULA
	SEP #PROC_FLAGS::ACCUM8
	LDA #2
	STA GAME_STATE + game_state::party_members,X
	INX
.ENDIF
@ADD_PAULA:
.IF .DEFINED(JPN)
	LDA @LOCAL11
	AND #$0004
	BEQ @ADD_JEFF
	LDA @LOCAL0B
	CLC
	ADC #.LOWORD(GAME_STATE)
	TAX
	SEP #PROC_FLAGS::ACCUM8
	LDA #3
	STA a:game_state::party_members,X
	REP #PROC_FLAGS::ACCUM8
	LDA @LOCAL0B
	INC
	STA @LOCAL0B
.ELSE
	REP #PROC_FLAGS::ACCUM8
	LDA @LOCAL11
	AND #$0004
	BEQ @ADD_JEFF
	SEP #PROC_FLAGS::ACCUM8
	LDA #3
	STA GAME_STATE + game_state::party_members,X
	INX
.ENDIF
@ADD_JEFF:
.IF .DEFINED(JPN)
	LDA @LOCAL11
	AND #$0008
	BEQ @ADD_POO
	LDA @LOCAL0B
	CLC
	ADC #.LOWORD(GAME_STATE)
	TAX
	SEP #PROC_FLAGS::ACCUM8
	LDA #4
	STA a:game_state::party_members,X
	REP #PROC_FLAGS::ACCUM8
	LDA @LOCAL0B
	INC
	STA @LOCAL0B
.ELSE
	REP #PROC_FLAGS::ACCUM8
	LDA @LOCAL11
	AND #$0008
	BEQ @ADD_POO
	SEP #PROC_FLAGS::ACCUM8
	LDA #4
	STA GAME_STATE + game_state::party_members,X
	INX
.ENDIF
@ADD_POO:
.IF .DEFINED(JPN)
	LDA @LOCAL0B
.ELSE
	REP #PROC_FLAGS::ACCUM8
	TXA
.ENDIF
	SEP #PROC_FLAGS::ACCUM8
	STA GAME_STATE+game_state::player_controlled_party_count
	BRA @CLEAR_SLOTS_CHECK
@CLEAR_REMAINING_SLOTS:
	.A16
.IF .DEFINED(JPN) ;wow
	CLC
	ADC #.LOWORD(GAME_STATE)
	TAX
	SEP #PROC_FLAGS::ACCUM8
	STZ a:game_state::party_members,X
	REP #PROC_FLAGS::ACCUM8
	LDA @LOCAL0B
	INC
	STA @LOCAL0B
.ELSE
	STZ GAME_STATE + game_state::party_members,X
	INX
.ENDIF
@CLEAR_SLOTS_CHECK:
.IF .DEFINED(JPN)
	REP #PROC_FLAGS::ACCUM8
	LDA @LOCAL0B
	CMP #$0006
.ELSE
	CPX #6
.ENDIF
	BCC @CLEAR_REMAINING_SLOTS
	JMP @REINIT_BATTLE
@START_BATTLE_MUSIC:
	LDA ENEMIES_IN_BATTLE_IDS
	LDY #.SIZEOF(enemy_data)
	JSL MULT168
	.A16
	CLC
	ADC #enemy_data::music
	TAX
	LDA f:ENEMY_CONFIGURATION_TABLE,X
	AND #$00FF
	JSL CHANGE_MUSIC
@CHECK_BUZZ_BUZZ:
	LDA #EVENT_FLAG::FLG_BUNBUN
	JSL GET_EVENT_FLAG
	CMP #0
	BEQ @CHECK_POSSESSED
	LDX #.LOWORD(BATTLERS_TABLE)+(TOTAL_PARTY_COUNT)*.SIZEOF(battler)
	LDA #ENEMY::BUZZ_BUZZ
	JSL BATTLE_INIT_ENEMY_STATS
	SEP #PROC_FLAGS::ACCUM8
	LDA #1
	STA BATTLERS_TABLE+(TOTAL_PARTY_COUNT)*.SIZEOF(battler)+16
	STZ BATTLERS_TABLE+(TOTAL_PARTY_COUNT)*.SIZEOF(battler)+battler::ally_or_enemy
	LDA #ENEMY::BUZZ_BUZZ
	STA BATTLERS_TABLE+(TOTAL_PARTY_COUNT)*.SIZEOF(battler)+battler::npc_id
@CHECK_POSSESSED:
.IF .DEFINED(JPN)
	REP #PROC_FLAGS::ACCUM8
	LDA #0
	STA @LOCAL07
.ELSE
	LDX #0
	STX @LOCAL07
.ENDIF
	BRA @POSSESSED_LOOP_ENTRY
@CHECK_POSSESSED_LOOP:
.IF .DEFINED(JPN)
	CLC
	ADC #.LOWORD(GAME_STATE)
	TAX
	LDA a:game_state::party_members,X
.ELSE
	REP #PROC_FLAGS::ACCUM8
	LDA GAME_STATE + game_state::party_members,X
.ENDIF
	AND #$00FF
	STA @VIRTUAL04
	STA @LOCAL08
	LDA @VIRTUAL04
