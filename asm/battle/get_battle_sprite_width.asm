
GET_BATTLE_SPRITE_WIDTH:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_PARAM_INT16 ;int sprite
	STACK_RESERVE_RETURN_INT16 ;int
	END_STACK_VARS
	DEC
	OPTIMIZED_MULT @VIRTUAL04, 5
	TAX
	INX
	INX
	INX
	INX
	LDA f:BATTLE_SPRITES_POINTERS,X
	AND #$00FF
	CMP #$0001
	BEQ @WIDTH_4
	CMP #$0003
	BEQ @WIDTH_4
	CMP #$0002
	BEQ @WIDTH_8
	CMP #$0004
	BEQ @WIDTH_8
	CMP #$0005
	BEQ @WIDTH_16
	CMP #$0006
	BEQ @WIDTH_16
	BRA @WIDTH_0
@WIDTH_4:
	LDA #$0004
	BRA @RETURN
@WIDTH_8:
	LDA #$0008
	BRA @RETURN
@WIDTH_16:
	LDA #$0010
	BRA @RETURN
@WIDTH_0:
	LDA #$0000
@RETURN:
	END_C_FUNCTION
