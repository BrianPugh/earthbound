
GET_BATTLE_SPRITE_HEIGHT:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_PARAM_INT16 ;int sprite
	STACK_RESERVE_RETURN_INT16 ;int
	END_STACK_VARS
	DEC
	OPTIMIZED_MULT @VIRTUAL04, 5
	TAX
	INX
	INX
	INX
	INX
	LDA f:BATTLE_SPRITES_POINTERS,X
	AND #$00FF
	CMP #$0001
	BEQ @HEIGHT_4
	CMP #$0002
	BEQ @HEIGHT_4
	CMP #$0003
	BEQ @HEIGHT_8
	CMP #$0004
	BEQ @HEIGHT_8
	CMP #$0005
	BEQ @HEIGHT_8
	CMP #$0006
	BEQ @HEIGHT_16
	BRA @HEIGHT_0
@HEIGHT_4:
	LDA #$0004
	BRA @RETURN
@HEIGHT_8:
	LDA #$0008
	BRA @RETURN
@HEIGHT_16:
	LDA #$0010
	BRA @RETURN
@HEIGHT_0:
	LDA #$0000
@RETURN:
	END_C_FUNCTION
