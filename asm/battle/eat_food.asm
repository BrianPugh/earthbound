	LDX @LOCAL03
	REP #PROC_FLAGS::ACCUM8
	TXA
	JSL RECALC_CHARACTER_POSTMATH_GUTS
	REP #PROC_FLAGS::ACCUM8
	LOADPTR MSG_BTL_GUTS_UP, @LOCAL00
