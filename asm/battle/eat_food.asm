
EAT_FOOD:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT32
	STACK_RESERVE_INT32
	STACK_RESERVE_INT16 "LOCAL02"
.IF .DEFINED(USA)
	STACK_RESERVE_INT32 "LOCALEB"
	@TEXTTMP := @VIRTUAL06
.ELSE
	@TEXTTMP := @VIRTUAL0A
.ENDIF
	STACK_RESERVE_INT16 "LOCAL03"
	END_STACK_VARS
	LDX CURRENT_TARGET
	LDA a:battler::id,X
	TAX
	STX @LOCAL03
	TXA
	DEC
	LDY #.SIZEOF(char_struct)
	JSL MULT168
	TAX
	LDA PARTY_CHARACTERS+char_struct::afflictions,X
	AND #$00FF
	CMP #STATUS_0::UNCONSCIOUS
	BNE @NOT_UNCONSCIOUS
	DISPLAY_BATTLE_TEXT_PTR MSG_BTL_KIKANAI
	JMP @RETURN
@NOT_UNCONSCIOUS:
	JSR APPLY_CONDIMENT
.IF .DEFINED(USA)
	MOVE_INT @VIRTUAL06, @LOCALEB
.ENDIF
	LDX @LOCAL03
	CPX #4
	BNE @NOT_POO
	LDA #2
	BRA @APPLY_FOOD_EFFECT
@NOT_POO:
	LDA #1
@APPLY_FOOD_EFFECT:
	MOVE_INTX @VIRTUAL06, @VIRTUAL0A
	CLC
	ADC @VIRTUAL0A
	STA @VIRTUAL0A
	LDA [@VIRTUAL0A]
	AND #$00FF
	TAY
	STY @LOCAL02
	MOVE_INT @VIRTUAL06, @VIRTUAL0A
	LDA [@VIRTUAL0A]
	AND #$00FF
	BEQ @CASE_HP_RECOVERY
	CMP #1
	BEQ @CASE_PP_RECOVERY
	CMP #2
	BEQL @CASE_HP_PP_RECOVERY
	CMP #3
	BEQL @CASE_RANDOM_STAT
	CMP #4
	BEQL @BOOST_IQ
	CMP #5
	BEQL @BOOST_GUTS
	CMP #6
	BEQL @BOOST_SPEED
	CMP #7
	BEQL @BOOST_VITALITY
	CMP #8
	BEQL @BOOST_LUCK
	CMP #9
	BEQL @CASE_HEAL_STATUS
	CMP #10
	BEQL @CASE_CURE_POISON
	JMP @CHECK_CONDIMENT_SPECIAL
@CASE_HP_RECOVERY:
	CPY #0
	BEQ @HP_FULL_RECOVERY
	TYA
	OPTIMIZED_MULT @VIRTUAL04, 6
	JSR TWENTY_FIVE_PERCENT_VARIANCE
	TAX
	BRA @DO_HP_RECOVERY
@HP_FULL_RECOVERY:
	LDX #30000
@DO_HP_RECOVERY:
	LDA CURRENT_TARGET
	JSR RECOVER_HP
	JMP @CHECK_CONDIMENT_SPECIAL
@CASE_PP_RECOVERY:
	CPY #0
	BEQ @PP_FULL_RECOVERY
	TYA
	JSR TWENTY_FIVE_PERCENT_VARIANCE
	TAX
