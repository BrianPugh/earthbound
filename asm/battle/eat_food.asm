	CPY #0
	BEQ @HP_FULL_PART
	TYA
	OPTIMIZED_MULT @VIRTUAL04, 6
	JSR TWENTY_FIVE_PERCENT_VARIANCE
	TAX
	BRA @DO_HP_PART
@HP_FULL_PART:
	LDX #30000
@DO_HP_PART:
	LDA CURRENT_TARGET
	JSR RECOVER_HP
	LDY @LOCAL02
	BEQ @PP_FULL_PART
	TYA
	JSR TWENTY_FIVE_PERCENT_VARIANCE
	TAX
	BRA @DO_PP_PART
@PP_FULL_PART:
	LDX #$7530
@DO_PP_PART:
	LDA CURRENT_TARGET
	JSR RECOVER_PP
	JMP @CHECK_CONDIMENT_SPECIAL
@CASE_RANDOM_STAT:
	LDA #$0004
	JSR RAND_LIMIT
	CMP #0
	BEQ @BOOST_IQ
	CMP #1
	BEQL @BOOST_GUTS
	CMP #2
	BEQL @BOOST_SPEED
	CMP #3
	BEQL @BOOST_VITALITY
	CMP #4
	BEQL @BOOST_LUCK
	JMP @CHECK_CONDIMENT_SPECIAL
@BOOST_IQ:
	LDA CURRENT_TARGET
	CLC
	ADC #battler::iq
	LDY @LOCAL02
	SEP #PROC_FLAGS::INDEX8
	STY @VIRTUAL00
	PHA
	REP #PROC_FLAGS::INDEX8
	TAX
	SEP #PROC_FLAGS::ACCUM8
	LDA __BSS_START__,X
	CLC
	ADC @VIRTUAL00
	PLX
	STA __BSS_START__,X
	LDX @LOCAL03
	REP #PROC_FLAGS::ACCUM8
	TXA
	DEC
	LDY #.SIZEOF(char_struct)
	JSL MULT168
	CLC
	ADC #.LOWORD(PARTY_CHARACTERS)+char_struct::boosted_iq
	PHA
	TAX
	SEP #PROC_FLAGS::ACCUM8
	LDA __BSS_START__,X
	CLC
	ADC @VIRTUAL00
	PLX
	STA __BSS_START__,X
	LDX @LOCAL03
	REP #PROC_FLAGS::ACCUM8
	TXA
	JSL RECALC_CHARACTER_POSTMATH_IQ
	REP #PROC_FLAGS::ACCUM8
	LOADPTR MSG_BTL_IQ_UP, @LOCAL00
	LDY @LOCAL02
	TYA
	STORE_INT1632 @TEXTTMP
	MOVE_INT @TEXTTMP, @LOCAL01
	JSL DISPLAY_TEXT_WAIT
	JMP @CHECK_CONDIMENT_SPECIAL
