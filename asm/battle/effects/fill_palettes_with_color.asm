
FILL_PALETTES_WITH_COLOR:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16 ;int
	END_STACK_VARS
	TAX
	STX @LOCAL01
	LDA #0
	STA @LOCAL00
	BRA @LOOP_CHECK
@FILL_ENTRY:
	LDX @LOCAL01
	PHX
	ASL
	TAX
	PLA
	STA PALETTES,X
	LDA @LOCAL00
	INC
	STA @LOCAL00
@LOOP_CHECK:
	CMP #$0100
	BCC @FILL_ENTRY
	LDA #24
	JSL SET_PALETTE_UPLOAD_MODE
	JSL WAIT_UNTIL_NEXT_FRAME
	END_C_FUNCTION
